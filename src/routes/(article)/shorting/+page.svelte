	<h2>Hvad er shorting?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Markedet boomer derudaf, men du tror at markedet snart vender og styrt dykker. Derfor er du
		interesseret i at byde imod markedet. Derfor låner du nogle aktier, som du kan sælge. Når
		aktierne forhåbentligt er faldet tilpas meget, så køber du aktierne tilbage, og returnere dem.
		Du har altså tjent penge på at sælge dyrt og købe lavt.
