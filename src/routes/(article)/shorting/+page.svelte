	<!-- wp:heading -->
	<h2>Hvordan fungere en shorting?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		For at oprette en shorting position, skal du først have en Margin-konto. Det er en konto hvor på
		der står et rådighedsbeløb, og er forbundet til en kapital-konto, hvorfra tab kan trækkes fra.
		Herefter tager du kontakt til din børsmægler med den position du gerne vil short.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Din børsmægler starter internt med at se, om der er nogle aktier som du kan låne. Hvis det ikke
		er muligt, kontakter de eksterne investeringsselskaber. Hvis de finder nok aktier eller
		investeringsbeviser til at dække din shorting position, så bliver de solgt på markedet og
		pengene kommer ind på din margin-konto.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når aktien stiger i pris, så falder beløbet på din margin-konto tilsvarende. Falder aktien, så
		stiger beløb på din margin konto. Når du beslutter dig for at lukke din shorting position, så
		køber din børsmægler aktierne eller investeringsbeviserne retur til nuværende markedspris.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis din shorting blev solgt til 100 kroner, og herefter købt til 65 kroner, så har du altså
		tjent differencen på 35 kroner før eventuel kurtage og beskatning.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Under normale aktieinvesteringer er risikoen forbundet med det investerede beløb. <strong
			>Når du shorter en aktie, er der ikke nogen begrænsning på hvor meget en virksomhed kan stige
			i værdi.
		</strong>Du skal derfor være opmærksom på at have kapitalen til at dække stigningerne. En god
		måde at begrænse sit tab på, er ved at oprette en <strong>stop-loss</strong> begrænsning på sin shorting
		position.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er risikoen ved shorting?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du opretter en shorting position, så sælger du lånte aktier til nuværende markedskurs. Her
		fra kan aktierne falde fra købspris til 0, hvilket ville være en god dag for dig. Desværre er
		der ikke en begrænsning på hvor meget en aktie kan stige. Du kan derfor komme til at tabe mange
		penge, hvis aktien stiger meget under din shorting.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Dertil er det også vigtigt at forstå hvordan ens shorting er sammensat, da udlåneren af dine
		shorting-position kan kræve aktierne tilbage med kort tids varsel. Derfor laver man oftest
		shorting positioner med aktier fra index fonde eller langtidsinvesterede selskaber.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du låner aktier til at lave en shorting position, skal der i nogle tilfælde udbetales et
		administrationsgebyrer. Dertil betaler man en løbende rente af de det lånte aktiers værdi.
	</p>
