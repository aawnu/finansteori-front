<h1>Obligationer - Tjen penge på andres gæld - En sikker investering?</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Hvis du følger med i investeringsverdenen, så har du sikkert hørt at obligationer er det sikre
		valg. Ved du hvad en obligation er, eller hvorfor den er mere sikker end andre investeringsmål?
		Målet med den her artikel er at blive klogere på, hvad er obligationer for en størrelse?
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:heading -->
	<h2>Hvad er obligationer?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		En obligation er det danske udtryk for “Bond”, vi kan også kan kalde et børsnoteret gældsbrev.
		Det kan også være en gruppering af flere gældsbreve, som er omdannet. Obligationer kan komme fra
		både staten og virksomheder, når de har brug for at optage eller finansiere et lån.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Et eksempel kan være når du optager et boliglån. Her kan realkreditten være udformet som et
		gældsbrev, der giver dem adgang til kapitalen du skal låne. Investorerne får altså et
		indestående med realkreditten og de har et indestående med dig. Forholdet mellem låneren og
		investoren er uafhængigt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Et andet eksempel kan være staten, som har brug for at finansiere en ny motorvej. Desværre har
		staten ikke råd til at betale forud, men kan finansiere det over 5-10 år via skatter. Her giver
		det mening for staten, at sælge gæld fra de større banker.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan er obligationer sammensat?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når en låner har brug for penge, så kontakter de et finansielt institut. Her laver man en
		risikoberegning, hvor man vurdere om låneren kan tilbagebetale gælden. Her kommer de typiske A
		og B bedømmelser fra, som man typisk høre om i amerikanske film.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Et gældsbrev indeholder nogle kriterier, som fortæller mulige investorer, om det er et godt
		investeringsmål. Heriblandt fastsættes hvor meget renten skal være, ud fra risikoen. Hvor lang
		tid lånet strækker sig over, og om lånet er inkonverterbart.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>For at udforme et eksempel, så opretter vi et gældsbrev fra staten med følgende egenskaber:</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li>Face value ("Lånet"): 1.500.000.000 kroner</li>
		<li>Løbetid: 5 år</li>
		<li>Kuponrente: 1,8%</li>
		<li>
			Inkonverterbar: Nej - <em
				>Kan indfries for samme 1.500.000.000 kroner, og skal ikke købes for gældende kursværdi</em
			>
		</li>
		<li>Hovedstol: 1.620.000.000 kroner - <em>lån og alle renter</em></li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Når gældsbrevet er udformet, så omdannes det til obligationer, som sælges på et børsmarked for
		obligationer. Man forventer ikke at sælge hele gælden til samme investor, så derfor deler vi
		lånet op i 100 kroners obligationer, svarende til 1.500.000.000 / 100 = 15.000.000 obligationer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hver obligation har altså en indre værdi på 100 kroner, det som bliver kaldet “Face Value”.
		Dertil skal du være opmærksom på, at <span
			class="has-inline-color has-luminous-vivid-orange-color"
			>obligationer typisk sælges i stakke fra 50.000 og op</span
		>. Hvis du ikke vil risikere den kapital, kan du
		<a
			href="https://finansteori.dk/borshandel/investeringsforeninger-fonde-spredning"
			class="rank-math-link">investere i ETF'er eller investeringsforeninger</a
		>, som fokusere på obligationer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan afbetales obligationer?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Obligationer er et lån, hvor man betaler en halvårlig eller årlig premium. Hvis du køber
		obligationer med face value til 100 kroner, med en kuponrente på 3%, så vil du modtage 3 kroner
		om året, per obligation.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis løbetiden er 5 år, så vil du altså modtage 15 kroner i samlet kuponrente for hver
		obligation du ejer. Når tiden er udløbet, vil ejeren blive tilbagebetalt dens face value på 100
		kroner imod at indfri gælden. Gælden bliver altså ikke afbetalt over tid, så man modtager renter
		af det fulde beløb, fra start til indfrielse.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når man handler med obligationer, er det desværre ikke helt så simpelt som at bruge et par
		tusinde kroner. Ofte kræver det at man køber flere hundrede tusinde obligationer ad gangen,
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvor er risikoen ved obligationer?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis udstederen af obligationen er en virksomhed, er der réel risiko for konkurs. Da
		obligationerne er et gældsbrev, vil konkursboet prioritere at betale gælden af først. Du mister
		de resterende kuponrenter og måske en del af indfrielsesværdien.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Investere man derimod i statsobligationer, fra lande som har en stærk økonomi. Er risikoen for
		konkurs meget lavere. Derimod har statsobligationer en lavere risiko og dertil udbetaler en
		lavere kuponrente. Risikoen er vurderet fra land til land, f.eks betragter man Danmark som et
		sikkert land, at købe statsobligationer fra.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad bestemmer værdien af obligationerne?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Kursen af en obligation er altid spekulativ. Hvor meget man kan tjene på at eje obligationen, i
		forhold til hvad man har betalt for den? Derfor er det vigtigt at kigge på dens face value, i
		forhold til historiske og fremtidige udbetalinger af kuponrente.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Vi har allerede afgjort, at obligationens indre værdi er 10.000 kroner, og udbetaler 300 kroner
		årligt over 5 års løbetid. Det giver os en total fortjeneste på 1.500 kroner. Efter 2 år, er vi
		interesseret i at sælge obligationen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Vi har modtaget 600 ud af 1.500 kroner, så køberen af vores obligation, kan altså tjene 1.500 -
		600 = 900 kroner på vores obligation. Hvis vi sælger obligationen til dens indre værdi på 10.000
		kroner, kan den næste ejer stadig tjene 9% på sin investering.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Da vi er mere interesseret i at indfri pengene, er vi villig til at give en rabat på
		obligationen, og sælger den derfor til 9.700 kroner, så vi stadig har en fortjeneste på 400
		kroner. Køberen kan nu tjene 12,4% på sin investering, og der derfor nemmere for os at sælge.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		På den måde spekulere man hele tiden over en obligations værdi i forhold til salg, fortjeneste
		og alternative obligationer til salg. Dertil kan der også være nogle spekulationer, om
		obligationen er sikker imod eventuel konkurs eller udsving.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan beskattes obligationer?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Obligationer og deres tilhørende udbytter/kuponrenter, kan både beskattes som aktieindkomst, og
		som personlig indkomst. Bliver du beskattet som almindelig kapitalindkomst, så bliver beløbet
		derfor medregnet i din personlige skat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Herunder tager vi stilling til beskatning som kapitalindkomst. Hvis du vil vide mere om
		beskatning af aktieindkomst, kan du læse <a
			href="https://finansteori.dk/borshandel/alt-du-burde-vide-om-aktieinvestering-komplet-introduktion"
			class="rank-math-link">vores komplette introduktion til aktieinvestering</a
		>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du køber en obligation, så er det for at modtage resterende udbetalinger af kuponrenter, og
		derefter at indfri obligationen imod dens indre værdi. Når du modtager renterne, skal beløbet
		oplyses til skat. Hvis renten er udbetalt i anden valuta, skal beløbet oplyses efter dagskursen,
		i danske kroner, på dagen de udbetales.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du modtager kuponrenter vil din bank eller udbyder oplyse beløbet til skat. Hvis de ikke
		gør, skal beløbet indberettes på <strong>Rubrik 31</strong>.&nbsp;Hvis renten kommer fra
		udlandske obligationer, skal de indberettes på <strong>Rubrik 431</strong>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du indfrier din obligation, skal der også betales skat af fortjenesten. Hvis du har et tab,
		kan du tilsvarende få et fradrag. Ved obligationer er der en bagatelgrænse på 2.000 kroner. Det
		vil sige at gevinsten eller tabet skal overkomme 2.000 kroner, før du skal beskattes eller opnår
		fradrag.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her bør din bank eller udbyder også oplyse afkastet til skat. Hvis ikke, skal det indberettes på <strong
			>Rubrik 32</strong
		>
		hvis obligationerne er handlet på et reguleret markedet, og ellers <strong>Rubrik 40</strong> hvis
		de ikke er.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong
			><em
				>OBS: Dertil er der regler for, hvis obligationerne er blåstemplede eller sortstemplede. I
				forhold til, om du kan få fradrag for tab.</em
			></strong
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Opsummering: Hvorfor investere man i obligationer?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når en virksomhed eller staten har brug for kapital, som de ikke kan fremskaffe under normale
		markeds vilkår. Måske er beløbet for stort til at den kommercielle bank kan godkende lånet, så
		kan de vælge at sælge gæld på markedet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du køber deres gæld, så modtager du en årlig rente af obligationernes face value, indtil
		obligationerne bliver indfriet. Ved at studere virksomheden eller staten bag obligationerne, så
		kan man afgøre hvor stor risikoen for konkurs er.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Obligationer variere ikke særlig meget i pris. Det gør ikke meget forskel, hvis du køber dem for
		at indfri dem. Du har et fast årligt udkast og en fast sats som bliver udbetalt i slutningen af
		obligationens levetid.
	</p>
