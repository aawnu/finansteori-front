	<!-- wp:heading -->
	<h2>Hvad er forskellen på en normal graf og en candlestick graf?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er vandt til at kigge på en streg graf, så har du være vandt til at følge markedets
		lukkepriser. Du går altså glip af udsvingende i de givende perioder. Du skal altså bruge hele
		grafen til at se, om aktien er stigende eller faldende. Med candlestick grafen kan du se hvert
		interval, om udsvingende ligger op til en stigning eller et fald.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		På grafen ovenfor ser du Apple's aktie i samme tidsperiode. På venstre side kan du kun se
		lukke-kursen for hvert interval, hvor imod candlestick viser udsvingende ved hver interval. Det
		kan godt virke lidt overvældende i starten, indtil man lærer at forstå de individuelle udsving.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du kigger i slutningen af begge grafer, så ligner det på streg kursen at aktien dykkede og
		steg. Ser man derimod på candlestick grafen, så opdager man at der bliver handlet lige meget,
		tiden har bare været afgørende for, om sidste handel har været høj eller lav.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det er vigtigt at du på grønne (stigende) pinde kigger på toppen og de røde (faldende) kigger på
		bunden. Med candlestick ser man mere generelt på dataens udvikling igennem en periode, og ikke
		hvad hvert interval sluttede på, medmindre der er store udsving.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvornår bruger man candlestick grafen?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Grafen giver et godt indblik løbende, da man kan indstille dens tidshorisont. Hvis man kigger på
		1 år, så vil hver pind vise 1 dag eller 1 uges handel. Det giver stadig fordelen af at kan se
		hvordan det generelle markedet udvikler sig på trods af op og nedture.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du <a
			href="https://finansteori.dk/borshandel/start-din-aktieinvestering-2020"
			class="rank-math-link">tjekker dine investeringer</a
		> med korte intervaller (dagligt eller ugentligt), så vil candlestick grafen give dig et mere balaneret
		overblik. Kigger du en gang om måneden eller i kvartalet, så har det ikke den store forskel for dig.
		Man kan sjældent forudse hvad der sker indtil næste gang du tjekker.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du kigger på din porteføljes udvikling, så giver det heller ikke mening at have candlestick
		grafen, fordi du ikke kan se hvilke aktier der gjorde hvilke udsving. Der får du altså mere ud
		af en streg graf, som blot viser den generelle lukningskurs per interval.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<em
			>Candlestick grafen bliver f.eks også anvendt til Forex kurser, da vekslingskurer oftest er
			meget volatile. Det er derfor vigtigt at kan forholde sig til trends i kursudsving.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Bonus: Streg vs Candlestick</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Istedet for at have 2 billeder side om side, som vi havde med streg grafen og candlestick
		grafen, så vil jeg gerne vise dig hvordan de overlapper hinanden. På den måde kan du se hvor
		meget information du går glip af, når du anvender streg grafen. Læg mærke til hvilke punkter
		grafen følger på op og nedgående pinde.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Ved at bruge candlesticks kan vi altså få et bedre billede over den generelle interesse i
		aktien, men også hvor store udsving der er, i forhold til start og slut pris på hvert interval.
		På følgende billede kan du se både udsving og dens indre kursændringer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Du skal også bemærke at pindene ikke starter og slutter i samme højde, da man tager ud fra
		første handel i et tidsrum. Det vil sige at hvis det er en minut visning, så kan sidste handel
		00:59 være til prisen 100 kroner, men i det nye minut 01:00 er første handel udført til 103
		kroner. Det skaber et 3 kroners mellemrum imellem slut og start af de 2 pinde.
	</p>
