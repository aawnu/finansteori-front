	<!-- /wp:more -->

	<!-- wp:paragraph -->
	<p>
		I den her artikel er målet at lærer dig at læse og bruge candlestick grafen. Så kan du bedre
		overskue markedet og dens kortsigtede fremtid. Der er også investorer som bruger candlestick
		grafen til at finde historiske mønstre, i håb om at historien gentager sig selv.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er candlesticks?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det første man skal lærer at at forstå hvilken farve er stigende og hvilken farve er faldende. I
		de fleste tilfælde er den stigende pind grøn eller blå, hvor den faldende rød eller grå. Her har
		vi den tykke streg, som definere hvor kursen startede og sluttede, og visuelt viser os den
		egentlig gevinst eller tab i tidsrummet. Dertil har vi de 2 små streger, som viser hvor høj og
		lav prisen var i tidsrummet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad viser candlesticks?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis vi har en minut-graf, så vil 1 søjle altså vise hele minuttet samlet i 1 pind. Lad os sige
		at 0:00 er kursen 100 kroner, 0:10 er kursen 115 kroner, 0:27 er kursen 92 kroner, 0:32 er
		kursen 105 kroner, og til sidste 0:52 er kursen 97 kroner.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du kigger på grafens udvikling over vores seneste minut, så blev der gennemførst 5 handler.
		På et aktivt værdipapir, kan der nemt komme flere tusinde handler i minuttet. Så bliver grafen
		lavet så du kun ser tidsrummet sidste handel. I det her tilfælde vil grafen gå fra 100 til 97,
		for at spare udsvingende imellem.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her får man mere ud af candlestick grafen, da den viser tidsperiodens start, højeste, laveste og
		slut. Jeg har været forudgående at lave streger på alle 4 punkter. Du kan se at punktet på 105
		ikke har et højdepunkt, og det er fordi den ikke udgøre et af de 4 nøgletal.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Først så kigger vi på højeste er 115 og laveste er 92, det skaber den lille streg i midten
