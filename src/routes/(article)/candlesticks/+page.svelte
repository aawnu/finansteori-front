<h1>Candlesticks - Det fulde overblik over en periode</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		I skolen og på arbejdet laver man typiske normale streg grafer, som en generel visning af om
		noget stiger eller falder i værdi. Candlestick grafer er derimod når man kigger på aktier, så
		kan der være flere tusinde handler i minuttet. Derfor bruger man istedet for candlestick grafen
		til at vise udviklingen inden for et tidsrum.
	</p>
	<!-- /wp:paragraph -->

