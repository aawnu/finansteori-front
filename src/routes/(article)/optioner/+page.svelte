<h1>Call og Put Optioner - Rettighederne til at købe og sælge aktier til forudbestemte priser</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Hvis du har en stærk tiltro til at aktier stiger eller falder i værdi, kan det give mening at
		spekulerer i optioner. Det giver dig rettigheden til at købe eller sælge aktier, til en
		forudbestemt pris, inden for en forudbestemt periode. Men det er ikke et krav at du køber eller
		sælger aktierne, derved navnet "option" på engelsk, hvilket på dansk betyder "mulighed" på
		dansk.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er ny til investering, anbefales du først <a
			href="https://finansteori.dk/borshandel/start-din-aktieinvestering-2020"
			class="rank-math-link">at lære omkring generel aktieinvestering</a
		>
		først. Dertil skal du være opmærksom på at optioner er avancerede investerings-instrumenter, som
		oftest bruges af banker og investeringsselskaber. For at opnå det fulde potentiale af
		optionhandel, bør du overveje at
		<a
			href="https://finansteori.dk/borshandel/invester-gennem-holdingselskab"
			class="rank-math-link">investerer gennem selskab</a
		>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er en option?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der findes 2 varianter af optioner, som af hver sin side af en mønt, opnår samme formål. Du kan
		handle Call Optioner, hvilket giver dig rettigheden til at købe aktier, hvor imod Put Optioner
		giver dig rettigheden til at sælge aktier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>CALL Optioner</strong> er en god måde at købe aktier, hvis du forventer at aktiekursen
		vil stige i pris, inden for en forudbestemt tidsperiode, til en forudbestemt pris. En god måde
		at huske det på, er ved at CALL referer til "kalde på".
		<em>På Nordnet kaldes disse også for "Købsoptioner".</em>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>PUT Optioner</strong> er derimod en god måde at sælge aktier, hvis du forventer at
		aktiekursen vil falde i pris, inden for en forudbestemt tidsperiode, til en forudbestemt pris.
		En god måde at huske det på, er ved at PUT referer til at "putte ud".
		<em>På Nordnet kaldes disse også for "Salgsoptioner".</em>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Optioner kan have en løbetid på alt fra 1 måned til 3 år. Det er derfor vigtigt at være
		opmærksom på hvornår optionen udløber, og til hvilken pris. Dertil skal du huske at optionen
		udløber, så hvis du er interesseret i at købe eller sælger, skal du indløse optionen inden
		udløbstidspunktet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Begge optioner har 2 sider, både køb og salg</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Uanset om du handler Call optioner eller Put optioner, så er der 2 sider af handlen. Der er dem
		som er interesseret i at købe rettighederne til at udføre optionen, og dem som sælger
		muligheden. For at kan købe Call optioner, skal der først være nogen som er interesseret i at
		sælge.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		De 2 sider af begge optioner er modsigende. Som investor i Put Optioner spekulerer man typisk i
		kursfald. Her betaler man en premium for at få rettighederne til at sælge til dagens kurs, så
		man kan udligne kursfaldet. På den anden side er der en som spekulere i at aktien stiger i
		værdi, og sælger derfor put optionen imod denne premium indtægt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det kan være lidt forvirrende i starten, så du får lidt mere information om de 2 individuelle
		muligheder.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Call Optioner, retten til at købe</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du som investor tror at en aktie vil stige i værdi inden for en forudbestemt periode, kan
		du vælge at købe Call optioner. De giver dig rettigheden til at købe aktier i fremtiden, som
		markeds værdien er idag. Det koster en premium til sælgeren, som tilbyder at sælge aktierne til
		dig, hvis du indløser optionen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det vil sige, at som køber, har du rettigheden men ikke obligationen til at købe andres aktier.
		Prisen er forudbestemt sammen med udløbsdatoen. Fordelen ved at købe disse rettigheder er, at du
		kun kan tabe det investerede beløb i optionen. Du er altså begrænset fra tabet af sælge
		aktierne, hvis de falder i værdi.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Sælgeren af optionen vil typisk spekulerer i at aktien falder eller ændre sig minimalt. På den
		måde kan de tjene penge på den betalte premium, når andre køber rettighederne fra dem.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Sådan tjener man penge på Call optioner</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du køber call optioner, så får du rettigheden til at købe aktier, til en forudbestemt pris
		og udløbsdato. Hvis du køber rettigheden til at købe Danske Bank til 80 kroner, for en 5 kroners
		premium. Så skal aktien altså stige minimum 5 kroner før optionen udløber, før at du tjener
		penge på handlen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Du skal derfor huske at indberegne prisen af optionen ind i købskursen du køber. Hvis aktien
		ikke overkomme købskursen + optionens pris, giver det ikke mening at udføre købskravet, så
		investeringen i optionen går derfor tabt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Put Optioner, retten til at sælge</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du som investor tror at aktien vil falde i værdi inden for en forudbestemt periode, kan du
		vælge at købe Put optioner. De giver dig rettigheden til at sælge aktier i fremtiden, som
		markeds værdien er idag. Det koster en premium til sælgeren, som tilbyder at købe dine aktier,
		hvis du indløser optionen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du tror at en aktie vil falde i pris, giver det muligheden for at kan sælge aktierne til en
		højere værdi end markedsprisen. Omvendt, hvis aktien er stabil eller stiger i værdi, vil du tabe
		penge på at sælge aktien, og investeringen går derfor tabt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Sælgeren af optionen spekulerer i at aktierne er stabile eller stiger i værdi. Sælgeren kan også
		spekulere i at aktien falder og så derefter, så er villig til at tabe en del af kursstigningen
		imod den premium han tjener på salget af optionen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Sådan tjener man penge på Call optioner</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du køber put optioner, så får du rettigheden til at sælge dine aktier, til en forudbestemt
		pris og udløbsdato. Hvis du køber rettigheden til at sælge dine Danske Bank aktier til 80
		kroner, for en 5 kroners premium. Så skal aktien altså falde minimum 5 kroner, før at du tjener
		penge på handlen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her skal du altså fjerne prisen af optionen, for at forstå hvor meget aktien skal falde, før
		slaget giver mening. Hvis Danske Bank er mere værd end <em>(80-7=)</em> 75 kroner ved udløb af din
		option, giver det ikke mening at udføre salget af aktierne. Din investering i optionen går derfor
		tabt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Til modsætning fra Call optioner, som har muligheden for ubegrænset fortjeneste. Så har Put
		optioner kun mellemrummet fra salgsprisen - premium, og til 0. Hvis aktien er 10 kroner, din
		premium er 2 kroner, så er din maksimale fortjeneste altså 8 kroner.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er risikoen ved optioner?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du handler optioner, så køber du et værdipapir med rettigheder. At købe optioner svarer lidt
		til at købe tegningsretter eller futures. Din reelle risiko er det investerede beløb alene, som
		kan gå tabt, såfremt investeringen den ikke er geared.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Beskatning af optioner (Privat)</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Optioner har en række forskellige retninger i forhold til beskatning. Hvis du udføre optionen,
		hvor du enten køber eller sælger aktier, kan du beskattes efter aktieavanceskatten. Hvis du ikke
		omdanner optionen til køb eller salg af underliggende aktier, så kan du blive beskattet efter
		ligningsloven af finansielle kontrakter, så ryger gevinsten eller tabet ind under personlig
		indkomstskat.
	</p>
	<!-- /wp:paragraph -->

