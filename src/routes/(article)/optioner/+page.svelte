	<!-- wp:heading -->
	<h2>Alternativ: Invester i futures</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har en fast beslutning om at vil købe eller sælge aktier i fremtiden, kan du også vælge
		at købe futures. Den egentlige forskel er, at med optioner har du et valg, om du vil udføre
		kontrakten. Med futures er du bundet til at købe eller sælge, når kontrakten når udløbsdatoen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		For at give et eksempel på en mulig future kontrakt. Vi har en landmand som malker køer og
		sælger mælken til Arla. Lige nu er prisen på mælk høj, men på grund af opbygningen af flere
		malke-farme, er du bange for at prisen vil falde. Du er derfor interesseret i at lave en future
		på mælkens pris, så du er sikret at få dagens markedspris når mælken skal sælges.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis mælken herefter falder i pris, er det en god handel for landmanden. Derimod er det en
		dårlig handel for Arla, som kunne have købt mælken billigere. Var mælken derimod steget i pris,
		ville landmanden tabe på den øgede værdi, hvor imod Arla med et smil får rabat på mælken.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		På samme måde kan du vurdere værdipapir imod fremtiden. I nogle investerings banker eller hedge
		fonde bruges det til at minimere risikoen i en given sektor. Det kan være at du ejer aktier i
		Arla, men tror at mælken vil stige. Derfor køber du Futures ved landmanden, så du kan tjene på
		at sælge mælken til Arla for profit.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her er fordelen, at hvis mælken falder i pris, så har du muligheden for at tjene tabet hjem
		igennem Arla, som køber mælken og har en større fortjeneste. Du har altså skabt en situation
		hvor du har muligheden for at tjene mange penge, men kun at tabe et mindre beløb.
	</p>
