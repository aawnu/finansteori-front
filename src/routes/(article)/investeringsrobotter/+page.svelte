	<h2>Hvad er fordelen ved investeringsrobotten i forhold til normal aktieinvestering?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Ved at investere gennem en investeringsrobot, så er omkostningerne lave. Du betaler oftest et
		lavt beløb når du indskyder eller indfrier dine penge, og dertil et årligt forvaltningsgebyr.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis vi tager udgangspunkt i June, så koster de cirka 0.94% om året, samlet. Det vil sige 94
		kroner, per 10.000 kroner du investerer, om året. Det indebære selvfølgelig ikke skatterne.
	</p>
	<!-- /wp:paragraph -->

