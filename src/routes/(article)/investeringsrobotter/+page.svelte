	<!-- wp:heading -->
	<h2>Hvordan beskattes investeringsrobotten?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Investeringsrobotten er som regel bygget op omkring investeringsforeninger, som hver holder sin
		risikogruppe. Under generelle vilkår bliver investeringsforeninger beskattet efter
		lagerprincippet. Det betyder at du årligt betaler skat, uanset om du indfrier din del af puljen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det er vigtigt at du har en indtægt, som kan betale den årlige beskatning. Hvis du indskyder
		10.000 kroner til robotten, og de bliver 25.000 kroner værd, så bliver du altså beskattet af
		15.000 kroners positiv afkast.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		De 15.000 kroner beskattes som aktieindkomst, efter 27/42% progressionsgrænsen. I 2020 er
		grænsen på 55.300, så du skal kun betale 27% skat af dine 15.000 kroner. Du skal altså betale
		4.050 kroner i skat, enten med eksterne midler, eller ved at sælge en del af din investering.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Vi anbefaler altid at du læser udbyderens egen hjemmeside omkring deres beskatning, da det kan
		variere fra robot til robot, og investeringsforening og investeringsselskab.
	</p>
