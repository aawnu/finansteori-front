	<!-- wp:heading -->
	<h2>Er investeringsrobotten for dig?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der er altid plads til at investeringsrobotten. Hvis du fortrækker at investere direkte på
		markedet, så kan du bruge investeringsrobotten som en sikkerhed. Hvis du ikke har tiden til at
		holde øje med markedet, kan det også være en nem løsning til at investerer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du ikke har maven til at se aktiepriser stige og falder i værdi, kan robotten også være en
		balanceret mulighed. Her ser du den totale værdi af din formue, og ikke de individuelle
		investeringers varierende værdier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det er nemmere at forholde sig til at ens totale værdi stiger eller falder X kroner, end at se
		på 15 forskellige investeringer leve sit eget liv. Man skal være i stand til at kan tabe penge,
		uanset hvordan man investere sine penge. Investeringsforeninger og investeringsrobotter gør det
		bare mere overskueligt, at se hvor meget men reelt vinder og taber.
	</p>
