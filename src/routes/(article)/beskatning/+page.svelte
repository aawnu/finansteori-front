	<!-- wp:heading -->
	<h2>Hvordan udfylder jeg min forskudsopgørelse?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		<a
			href="https://skat.dk/skat.aspx?oid=2234502"
			target="_blank"
			aria-label=" (opens in a new tab)"
			rel="noreferrer noopener nofollow"
			class="rank-math-link">Hvert år får du et skattekort</a
		> uddelt elektronisk på skats hjemmeside. Her har du muligheden for at lave en forskudsopgørelse.
		Her får du en lang række skatte rubrikker, som hver tager forbehold til en bestemt beskatning. Når
		du åbner din forskudsopgørelse, så anbefaler jeg at du starter fra toppen og langsomt bevæger dig
		ned.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du finder en rubrik der dækker over en indtægt, et fradrag, eller på anden vis passer til
		din situation. Så er det vigtigt at du laver en beregning over det fulde år, og indsætter tallet
		her. Hvis du tjener 26.000 kroner om måneden, så er det vigtigt at du skriver 312.000 i
		rubrikken.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du tjener mindre end personfradraget, er det stadig vigtigt at oplyse det fulde beløb. Selv
		om skat ikke skal opkræve eller inddrage skatter fra pengene, så er det vigtigt at de kender
		dine indtægter, så du oplyser at du ikke tjener pengene sort (ulovligt).
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis din situation ændre sig, så er det vigtigt at du igen logger ind og ændre din
		forskudsopgørelse. På den måde kan skat tilpasse hvor meget du skal betale i skat, og fordele
		dem ud på årets resterende måneder. Så ender du ikke med at skal betale hele din sket når
		opgørelsen sendes ud. Oftest anbefaler man at skrive 1-2% for meget ind i sin opgørelse, så man
		istedet for får penge tilbage, eller kan tåle hvis indtægter <strong
			>afviger forventningerne</strong
		>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan får jeg mere i skattefradrag?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Skattefradrag er en sjov størrelse, da man nemt kan argumentere at det mest hjælper de rige. For
		at opnår et skattefradrag, skal man nemlig have noget skat at modregne. Dertil kræver det at du
		har penge efter skat, som du kan bruge på ting som giver dig et skattefradrag.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Så inden vi kan kigge på hvordan vi får mere i skattefradrag, skal vi først vide: Hvad er et
		skattefradrag? Et skattefradrag er oftest når du bruger penge på noget, som gavner landets
		økonomi og udvikling.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		For at give et eksempel på et fradrag, som mange ikke tænker over de får, er for Fagligt
		kontingent. Du får altså et fradrag af de første 6.000 kroner, du betaler til en fagforening.
		Hvis du betaler 479 kroner om måneden til din fagforening, altså <em>479*12=</em> 5.748 kroner om
		året, så kan du modregne dette beløb af din skat.
	</p>
