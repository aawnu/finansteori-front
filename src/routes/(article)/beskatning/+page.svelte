		>. I 2020 er topskatten på 15%, som du skal ligge oven i din kommune skat, af den del som
		overstiger topskattens bundgrænse efter AM-bidrag.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
