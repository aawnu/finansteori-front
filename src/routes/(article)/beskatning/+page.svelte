	<!-- wp:heading -->
	<h2>Jeg er stadig ikke helt med, hvordan er fradrag en fordel for mig?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Du har sikkert hørt om folk der har gjort noget, fordi de kan trække det fra i skat. Måske gav
		du et lille grin, som om du forstod hvad det betød, selv om du ikke gjorde. Hvis du stadig ikke
		helt forstår hvad fradraget er, eller hvordan det fungere, så lad mig give dig et mere konkret
		eksempel.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		For at gøre det nemt, så bruger vi vores udregning ovenfor, hvor vi tjener 26.000 kroner om
		måneden, betaler en årlig skat på 88.302,23 kroner og har 16.561,48 kroner tilbage til dig selv
		hver måned.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du køre til og fra arbejde i din egen bil, så betaler du for brændstof og bilen får noget
		slidtage. Det er noget du dækker med dine 16.561,48 kroner. Måske tanker du for 1.000 kroner og
		har en forventet slidtage på 600 kroner om måneden. Da du har 26 kilometer til og fra arbejdet
		hver vej, altså 52 kilometer om dagen, så kan du få et fradrag.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		De første 24 kilometer får du ingenting, så har vi 28 kilometer tilbage. Af de 28 kilometer får
		du 196 øre per kilometer, altså 1,96 kroner, svarende til 54,88 kroner om dagen. Hvis vi går ud
		fra cirka 23 arbejdsdage på en måned, så har du et månedligt fradrag på 1.262,24 kroner.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det beløb kan du tilføje dit dine fradrag. Det betyder at din beskatning bliver mindre og du
		enten har flere penge udbetalt hver måned - eller får penge tilbage i skat, når året er omme.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det kræver kun at du tjener nok penge, til at betale skat. Man kan kun få fradrag fra den skat,
		man ellers skulle have betalt. Hvis vi tager udgangspunkt i vores eksempel, ville du maksimalt
		kunne opnå et fradrag på de 88.302,23 kroner. Det ville næsten aldrig kunne lade sig gøre at
		opnå 100% fradrag, med mindre du betaler minimalt i skat, på grund af fradragslofter og at de
		færreste ting giver 1:1 fradrag.
	</p>
