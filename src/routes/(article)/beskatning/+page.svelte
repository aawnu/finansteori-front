<h1>Beskatning - Sådan beregner du skat af kapital indkomst</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Hvis du ikke tager en økonomisk uddannelse, så er der stor chance for at skolen mangler at lære
		dig omkring skat. Hvis du tænker tilbage på folkeskolen og måske også gymnasiet, hvor meget
		lærte du egentligt omkring skat?
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:heading -->
	<h2>Du skal betale skat af alle intægtskilder</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		På et tidspunkt kommer du til at have et arbejde, måske allerede imens du er studerende. Når du
		får et arbejde, så får du en kontrakt hvor på der står hvor meget du kommer til at tjene. Hvis
		du ikke ved hvad skat er, så får du en overraskelse, når du ikke får udbetalt hvad du har tjent.
		Hvis du også modtager S.U. risikere du at have endnu færre penge, da du måske betaler B-skat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		I danmark har var 2 indkomstklasser. Vi har A-skat, som er vores primære (kapital)
		indtægtskilde, hvor vi modregner vores frikort og skattefradrag. Derefter har vi B-skat, som er
		alle andre (kapital) indtægtskilder, hvor vi betaler fuld skat af enhver krone. Det betyder ikke
		at skat snyder dig fra dit frikort og dine fradrag, de bliver dog først opgjort ved
		årsopgørelsen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<em
			>Jeg gør lige opmærksom på at det her omhandler kapital indtægt, altså når du får løn fra en
			arbejdsgiver eller ved at udføre freelance arbejde. Dette inkludere ikke <a
				href="/aktieinvestering"
				class="rank-math-link">aktieskat</a
			>, renteskat eller skatter i andre kategorier.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvor meget skal jeg betale i skat?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Dit skattegrundlag er hovedsageligt opgjort af 8% arbejdsmarkeds bidrag (AM) og derefter statens
		og kommunens skattegrundlag. Hvis du vil have dit specifikke skattegrundlag, kan du tjekke på <a
			aria-label=" (opens in a new tab)"
			href="https://www.skm.dk/skattetal/satser/danmarkskort-over-kommuneskatter/"
			target="_blank"
			rel="noreferrer noopener"
			class="rank-math-link">Danmarkskort over Kommuneskatter</a
		>. Lad os tage et kig på København, så du kan få en forklaring af hvor pengene forsvinder hen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>København kommune beskattes altså med 36,71%.</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		23,80% går til kommunen selv, som de kan bruge på veje, offentlig transport, skoler og andet.
		Kirken tager 0,80%, som går til at dække deres udgifter og giver dig retten til at blive
		begravet ved kirken. Du kan frasige den ret og slippe for skatten ved at <a
			aria-label=" (opens in a new tab)"
			href="https://afmeldkirkeskat.dk/"
			target="_blank"
			rel="noreferrer noopener nofollow"
			class="rank-math-link">afmelde kirkeskatten</a
		>. Til sidst har vi bundskatten på 12,11%. Det er skatten som går til selve staten, som går til
		at betale nationale udgifter, som ikke er kommune specifikke.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan udregner jeg min A-skat?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du kender dine beskatnings vilkår, så mangler du at kende dine skattefradrag. Dit største
		skattefradrag er som standard dit frikort, som skat kalder Personfradrag. I 2020 er dit
		personfradrag på 46.500 kroner (36.100 kroner, hvis du er under 18) om året.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		For at give en teoretisk udregning, så lad os sige at du tjener 26.000 kroner om måneden, før
		skat. Det omregner vi til alle 12 måneder, så vi får din årlige indtægt: <em>26.000*12=</em> 312.000
		kroner om året.
	</p>
