	<!-- wp:paragraph -->
	<p>
		Ovenfor ser du den typiske udregning af din skat i dens rå form. Det kan være du også betaler
		indtil pension eller har en kantine ordning som køre over din løn. Du bør altså kun bruge selve
		beregningen til at finde ud af om din skat er sat nogenlunde rigtigt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		B-skat er mere lige til, da man ikke tager højde for fradrag. Her tager du blot din månedlige
		(eller årlige) løn, trækker 8% AM-bidrag fra og så 36,71% beskatning fra - eller hvad
		beskatnings procenten i din kommune er.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du vil vide hvad din effektive beskatning har været, så tager du årlige udbetaling op imod
		årlige indtægt, og har vi betalt <em>100-(198.737,77/312.000,00*100)=</em>
		<strong>36,301%</strong> i skat efter fradrag. Regnestykket fortæller os hvor meget vi har tilbage
		efter skat (inde i parantesen), så derfor siger vi 100 minus parantesen, for at se hvor stor del
		skatten udgjorde.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan udregner jeg top-skat?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har fundet dig selv en position, hvor du skal betale topskat. Så kan du finde
