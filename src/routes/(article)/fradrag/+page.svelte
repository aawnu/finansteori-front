<h1>Skattefradrag - Sådan fungere det fradrag i skatten (2022 satser)</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Skat er en af de ting, som de fleste bare betaler, uden helt at forstå. Når året er omme, og man
		har rettet sin årsopgørelse, så bruger man bare det til sin næste forskudsopgørelse også. Sådan
		oplever folk altid at have skattegæld i slutningen af året, hvis man har opnået større indtægter
		eller brugt færre fradrag gennem året.
	</p>
