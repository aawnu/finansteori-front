	<!-- wp:heading -->
	<h2 id="htoc-opgiv-forventede-indt-gter-p-forskudsopg-relsen">
		Opgiv forventede indtægter på forskudsopgørelsen
	</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det danske skattesystem er bygget op omkring indtægter og fradrag. Hvis du ikke har været inde
		at redigere din forskudsopgørelse, er der en stor chance for, at den bruger sidste årsopgørelse
		for igangværende år.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Skat giver dig muligheden for at oplyse dine forventede indtægter og fradragsberettigede
		udgifter på forhånd, så de kan beskatte dig bedst muligt i løbet af året. Sådan undgår du også
		at skal have en masse penge tilbage fra skat, eller værre, at skal af med penge når du skal
		udfylder opgørelsen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Der er forskellige rubrikker i forskudsopgørelsen, men det er kun relevant for dig at kigge på
		dem du kan svaret på i forvejen. Nogle af rubrikkerne er automatisk udfyldt, f.eks aktiehandel
		gennem dansk-registreret broker.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du for eksempel tjener 30.000 kroner før skat om måneden, så skal du indtaste den
		forventede indtjening for hele året, svarende til 30.000x12= 360.000. Hvis du halvvejs gennem
		året stiger i løn, til f.eks 32.000 kroner, så skal du gå ind og ændre din opgivelse til
		30.000x6 + 32.000x6 = 372.000. Sådan ender du ikke med at skylde skatten af de ekstra 2.000
		kroner om måneden i 6 måneder.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<em
			><strong>OBS:</strong> Hvis du er usikker på hvordan du skal beregne eller opgive nogle
			rubrikker så
			<a href="https://skat.dk/kontakt" target="_blank" rel="noreferrer noopener"
				>kontakt Skattestyrelsen</a
			> for at få direkte rådgivning og undgå fejl eller overraskelser senere.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2 id="htoc-s-dan-beregnes-din-skat-i-hovedtr-k">Sådan beregnes din skat i hovedtræk</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det er ikke muligt at give en beregning der passer på alle, da skat er en individuel sag baseret
		på nogle overordnet skatteregler. Derfor laver jeg en overordnet skatte beregning her.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<em
			><strong>OBS:</strong> Dette eksempel tager kun udgangspunkt i personfradrag og befordringsfradrag
			for at holde det simpelt for alle at følge med. Der tages heller ikke højde for topskat, pension
			eller ATP. Du bør altid tage stilling til hvert punkt på din lønseddel og følge op på dem du ikke
			forstår.</em
		>
	</p>
	<!-- /wp:paragraph -->

