	<!-- wp:heading {"level":3} -->
	<h3 id="htoc-s-dan-beregner-du-befordringsfradraget">Sådan beregner du befordringsfradraget</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Befordringsfradraget er delt op i flere kilometer-grupper, så du får ikke samme sats for alle
		kilometer du kører til og fra arbejde om dagen. Det er beregnet ud fra totale kilometer kørt til
		og fra arbejde på en dag, så hvis du har 50 km hver vej, kører du 100 km om dagen. <strong
			>Her er satserne for 2022:</strong
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<!-- wp:list-item -->
		<li>0-25 km giver fradrag på 0 kr.pr.km.</li>
		<!-- /wp:list-item -->

		<!-- wp:list-item -->
		<li>25-120 km giver fradrag på 2,16 kr.pr.km.</li>
		<!-- /wp:list-item -->

		<!-- wp:list-item -->
		<li>+120 km giver fradrag på 1,08 kr.pr.km.</li>
		<!-- /wp:list-item -->
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Her er vigtigt at forstå at det fungere som trappetrin og at satsen ikke er baseret på alle
		kilometer hvis den totale afstand falder inden for en gruppe. I overstående sektion bruger vi
		100 km om dagen som eksempel for vores befordringsfradrag. Her er hvordan vi er kommet frem til
		40.986 kroner i fradrag for året.
	</p>
	<!-- /wp:paragraph -->

