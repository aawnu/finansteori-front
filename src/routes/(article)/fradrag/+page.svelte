	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Note B</strong>: Som privatperson må man ikke opkræve moms, men hvis du sælger
		<strong>momspligtige ydelser/produkter</strong> for mere end 50.000 på en 12-måneders periode, så
		skal du momsregistreres. Det er ligemeget om det er som enkeltmandsvirksomhed eller ApS.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Note C</strong>: Hvis dine B-indtægter er salgs-fremmende/relateret, så kan du trække
		udgifter fra. Hvis du for eksempel sælger en hjemmeside til 5.000 kroner, men det koster dig
		1.200 kroner for domæne og hosting, så skal du kun betale B-skat af 5.000-1.200= 3.800 kroner.
		<em
			>(Husk at gemme kvitteringer i minimum 5 år, fysisk eller digitalt, ref bogføringsloven §10)</em
		>
	</p>
