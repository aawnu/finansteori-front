	<!-- wp:heading -->
	<h2 id="htoc-rsopg-relsen-er-slutresultatet-for-ret">Årsopgørelsen er slutresultatet for året</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Forskudsopgørelsen er vores forventninger til årets økonomi, hvad vi regner med at have af
		indtægter og bruge af fradrag. Her bliver hele årets indtægter og fradrag fordelt ud på alle
		årets måneder, så din arbejdsgiver kan indbetale acontoskat af din løn eller du kan indbetale
		acontoskat af b-indkomster (hvis du f.eks freelancer eller er selvstændig).
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Årsopgørelsen fungere ligesom din forskudsopgørelse. I stedet for at bruge dine oplysninger, så
		opgøre de hvor meget din arbejdsgiver, bank og investerings platform har oplyst om dig. Når alle
		tallene er oplyst og lagt ind i systemet, så får du muligheden for at lave de sidste rettelser,
		i tilfældet af at nogle rubrikker mangler eller er forkerte.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Sidst, når du har rettet din årsopgørelse, så laver skat den endelige beregning på hvor meget
		der er betalt ind på din skattekonto og hvor meget du skulle have betalt. Hvis der står for
		meget på din skattekonto, så får du penge tilbage, ellers opkræver de det manglede beløb.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Der er mange som vælger at betale for meget i skat, for at få penge tilbage som en slags "gave"
		fra dem selv til sig selv. Når dagen er omme er det vel altid sjovere at få lidt tilbage, end at
		skal af med penge. Hvis dette er noget du gerne vil opnå, så sæt et lidt større beløb ind på din
		forskudsopgørelse, så du får opkrævet lidt mere i skat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2 id="htoc-b">Beskatning af B-indkomster</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har en indtægtskilde, der ikke fratrækker AM bidrag eller skat, så skal du selv sørge
		for det. For at adskille dette på dit skattekort, skal disse indtægter oplyses som
		B-Skat/Honorar.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det kan for eksempel være indtægter fra udlandet, ved at føre hobby-virksomhed, eller hvis du
		modtager penge (honorar/donationer) i relation til en ydelse du udfører.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her er det vigtigt at notere, at du skal oplyse alle indtægter til skat, uanset beløb størrelse
		eller akkumulation. Det gør altså ingen forskel om du tjener 1.000 kroner hele året, eller 1.000
		kroner hver måned. Beløbet skal oplyses til skat, så de kan opkræve beskatningen fra dig.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Normalt skal forskudsopgørelsen reflektere forventede indtægter for hele året. Det er derfor
		ændringer først træder i kræft måneden efter, men nogen gange ændre ens økonomi sig i løbet af
		året. Det er altså okay at tilpasse tallene løbende, som du kender udfaldet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3 id="htoc-s">Eksempel på B-skat udregning</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		For et give et eksempel. Hvis du tjener et variabelt beløb fra udlandet hver måned, så giver det
		måske mere mening at opdatere forskudsopgørelsen hver eller hver anden måned.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		I januar tjener du 1.300 kroner og i februar tjener du 1.800 kroner. Så kan ud i marts indsætte
		3.100 kroner i forskudsopgørelsen. Marts og April tjener du sammenlagt 2.400 kroner, så i maj
		tilpasser du forskudsopgørelsen til 3.100 + 2.400 = 5.500 kroner, altså det totale beløb for
		året.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du tjener et lavere beløb op året, så giver det mest mening at betale skatten ved
		årsopgørelsen, men du kan også vælge at betale løbende acontoskat, så du ikke står med en større
		skattegæld ved opgørelsen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		B-indkomster bliver beskattet efter normal AM og Kommunale skattesatser, men der fratrækkes ikke
		fradrag eller andet i denne opgørelse. Det vil sige at dine 5.500 kroner skal du først trække AM
		på 8%, og så af det nye beløb trække 36,69% (kbh sats 2022).
	</p>
	<!-- /wp:paragraph -->

