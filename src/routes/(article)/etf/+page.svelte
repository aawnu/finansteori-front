		holde den på din ASK.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du har din investeringskonto, som giver dig adgang til børsmarkedet. Så kan du begynde at
		undersøge hvilke ETF der giver mening for din investering. Når du har fundet dem, så kan du være
		heldig at de handles ofte, så du nemt kan købe dig ind. Du kan også være mindre heldig, at folk
		holder fast på deres investeringsbeviser, så du er nød til at give et bud og håbe at nogen går
		med på den.
	</p>
