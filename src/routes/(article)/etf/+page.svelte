	<!-- wp:heading -->
	<h2>Hvad koster det at eje investeringsbeviser i en ETF?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du har fundet og købt dine investeringsbeviser, så skal du huske at puljen forvaltes af en
		fond eller et investerings-institut. De opkræver nogle gebyr for at håndtere porteføljen, men
		prisen variere meget ud fra bestemte kriterier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li>Er puljen aktivt eller passivt forvaltet?</li>
		<li>Er puljen forsikret, f.eks imod valuta-tab?</li>
		<li>Er puljen investeret i flere valutaer (intern valuta-veksling)?</li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Det er ikke alle omkostningerne du får at se, da de er direkte forbundet med investeringerne. Du
		bliver typisk kun gjort opmærksom på fondens omkostninger, som for eksempel administrations
		gebyr. Her skal du være opmærksom på fondens basis-valuta, da alle investeringer opgøres i den
		valuta. Hvis der investeres i flere forskellige lande, har dagens vekslings-kurs direkte
		indflydelse på værdien af ETF'en.
	</p>
