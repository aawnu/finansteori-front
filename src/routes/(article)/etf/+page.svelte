<h1>ETF'er - Investering i lukkede og børsnoterede fonde</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		ETF er på dansk en børsnoteret lukket investeringsforening. Du kan ikke indskyde eller trække
		penge ud af en ETF, da man handler investerings-beviserne mellem investorer på børsen, og ikke
		med fonden selv. Det betyder at fonden er tættere knyttet på sine investeringer, da de kun har
		intern kapital at arbejde med. Bliv klogere på hvordan du kan integrere ETF'er i din porteføljer
		her!
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:heading -->
	<h2>Hvad er en ETF?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		ETF er en forkortelse for "Exchange-Traded Fund", og svare til en lukket værdipapir fond i
		danmark. Det betyder at der et et bestemt antal investeringsbeviser, ligesom et selskab har et
		bestemt antal aktier. Den primære og vigtige forskel, er at aktier giver ejerskab over
		selskabet, men investeringsbeviser giver ejerskab over den investerede pulje.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Normale investeringsforeninger og fonde har åbne puljer, hvilket betyder at du til enhver tid
		kan forøge puljens størrelse med dine egne penge. Når du investere i dem, så modtager fonden
		pengene og udsteder et investeringsbevis, som du senere kan indløse imod bevisets værdi ved
		indløsningen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Med andre ord. Ved åbne fonde smider du penge i puljen, hvor værdien følger puljens værdi. Med
		lukkede fonde køber og sælger du ejerskabet over en pulje med andre investorer, og ikke med
		fonden selv. Du kan kun blive ejer af en lukket fond hvis nogen sælger deres
		investeringsbeviser, og du kan kun realisere dit afkast hvis du kan sælge investeringsbeviset
		til en ny investor.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvorfor investere man i en ETF?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det er populært at investere sine penge i investeringsforeninger og fonder for at opnå en
		spredning i markedet. Ved at købe sig ind i disse foreninger og fonder, så slipper man for selv
		at analysere og forvalte porteføljen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis man investere sine penge i en aktivt forvaltet fond, så sidder der en række investorer, som
		aktivt tager stilling til investeringerne. De forsøger på bedste vis at analysere det fremtidige
		aktiemarkedet, for derefter at investere penge i den sektor eller branche.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		En passiv forvaltet fond følger derimod oftest et indeks. Det vil sige at pengene investeres ud
		fra forudbestemte rammer, i forudbestemte aktier, obligationer, eller andre/alternative
		investeringsmål.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Ved at købe 1 investeringsbevis i en fond, eller ETF, så kan man opnå en spredning på flere
		hundrede aktier. Det giver dig en mere stabil investering, da du følger den totale værdi af
		aktierne. Hvis nu 10 aktiekurser udløber -3, +4, -11, +5, +7, -6, +23, -2, +9, +8, så bevæger
		din investering sig +34 den dag, hvilket er tallet du ser, og ikke kursændringerne på de mange
		underlæggende investeringer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan investere man i en ETF?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er interesseret i at eje investeringsbeviser i en ETF, så skal du først oprette en
		investeringskonto. <a
