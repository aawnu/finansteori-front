	<!-- wp:heading -->
	<h2>Hvordan beskattes en ETF?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Fordelen ved at investere i ETF'er er at man altid er lagerbeskattet. Det betyder at du hvert år
		bliver beskattet efter investeringens udvikling i markedet. Hvis værdien er steget, så beskattes
		du af forskellen mellem køb eller sidste opgørelse og hvad værdien er ved opgørelsen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		For at give dig et eksempel, sidste år købte du 100 investeringsbeviser til 100 kr.p.stk som var
		200 kr værd de 31 december. Du betalte derfor skat af de 100 kroners fortjeneste per aktie. I år
		køber du yderligere 100 investeringsbeviser midt på året til 220 kr.p.stk. Ved næste opgørelse
		den 31 december er investeringsbeviserne 285 kr.p.stk værd. Det vil sige de 100 beviser fra
		sidste har har en stigning fra 200 til 285 og de nye har en stigning fra 220 til 285. Du skal
		altså beskattes af 100*85 + 100*65 = 15.000 kroner totalt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Du bliver altså beskattet af den individuelle udvikling per aktie. Hvis du vælger at købe 100
		beviser i januar og sælge dem i marts, så bliver de også talt med ved opgørelsen den 31
		december.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		I starten af Januar vil investerings.platformen typisk opgøre din årlige udvikling,
		indrapportere beløbet til skat og derefter trække aconto-skat fra den investeringskonto, som
		ETF'en ligger på. Du skal derfor sørge for at have eksterne midler til rådighed, så du du kan
		dække skatten på investeringskontoen.
	</p>
	<!-- /wp:paragraph -->

