<h1>Investeringsforeninger, fonde og ETF'er giver stor spredning til få penge</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Du vil gerne investere dine penge, men har ikke tiden eller overskuddet. Måske foretrækker du
		bare ikke at stå for det selv, da du ikke ved hvad du burde investere i. Ved at undersøge
		markedet for investeringsforeninger, så kan du kommer igang med at investere både nemt og
		billigt. Det giver dig muligheden for at investere i kategoriserede brancher, værdipapirtyper,
		lande og risiko.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:paragraph -->
	<p>
		Investeringsforeninger har oftest nogle helt klare retningslinjer, som afgøre hvordan de må
		investerer sine penge. Det kan for eksempel være en index fond, som investere i 500 store
		handels- og service virksomheder i Amerika, også kaldet S&amp;P500 Indekset. Måske investerer de
		i obligationer fra forskellige europæiske lande og virksomheder.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er en investeringsforening?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Man anvender normalt begræbet "investeringsforening", men der er faktisk 3 selskabsformer,
		hvorigennem du kan investere enten aktivt eller passivt. Du kan både investere dine penge i <strong
			>fonder</strong
		>, <strong>investeringsselskaber</strong> og <strong>investeringsforeninger</strong>. Det
		fungere ved at man skyder penge ind i en fælles pulje, som så investeres ud fra nogle
		forudbestemte retningslinjer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Den primære forskel er at et investeringsselskab må geare deres investeringer. De må altså
		optage et lån udefra, så de kan lave større investeringer, end puljen indeholder.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		En vigtig note er at investeringsinstitutter ikke producere eller yder andre tjenester, end at
		investere i værdipapirer, valutaer og obligationer.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan investere jeg gennem investeringforeninger?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
