	<h3>Generelle skattevilkår</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der er flere forskellige beskatningsgrundlag, ud fra hvordan instituttet er opsat og hvad de
		investere i. Her får du en oversigt over de generelle skatteforhold.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Investeringsforeninger </strong>er beskattet typisk efter realisationsprincippet, så du
		er skal betale skatten, når du sælger dine investeringsbeviser i foreningen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Akkumulerende foreninger</strong> er et begræbet, for de foreninger som ikke udbetaler udbytte.
		Deres formål er at geninvestere udbyttet internt i puljen, så den heletiden gror i værdi. Her bliver
		du beskattet som personlig indkomst efter lagerprincippet. Afkastet bliver inkluderet i din normale
		skat, og kan påvirke din beskatning, hvis du rammer top-skat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Investeringsselskaber </strong>er beskattet efter lagerbeskatnings-princippet, så du er
		beskattet årligt, uanset om du sælger dine investeringsbeviser i selskabet. Det betyder at du
		beskattes af positivt afkast og udbytterne i slutningen af året, og ikke ved salget.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>“Fonde”</strong> henviser typisk til konstruktionen <strong>“Værdipapirfonde”</strong>,
		og vil beskattes ud fra sit formål og hvad deres primære investeringer er. Der er ikke meget
		specifik information om, hvordan de beskattes i generelle sammenhæng. Kontakt udbyderen af
		fonden, hvis det ikke står på deres hjemmeside.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad koster det at investere gennem investeringsforeninger?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Omkostningerne ved at investere i et investeringsinstitut er som regel baseret på, om din
		portefølje er aktivt eller passivt forvaltet. Altså om der sidder investorer, som aktivt
		håndtere investeringerne, i håb om at forøge dit afkast.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du investere i et passivt institut, så vil de oftest følge et indeks. Det kan for eksempel
		være <a
			href="http://en.xact.se/Our-ETFs/Equity/XACT-OMXC25/#tab=underlying"
			target="_blank"
			aria-label=" (opens in a new tab)"
			rel="noreferrer noopener nofollow"
			class="rank-math-link">XACT OMXC25 indekset</a
		>, som indeholde de 25 største virksomheder i danmark. Når de modtager dit indskud, så fordeler
		de pengene på de 25 virksomheder, som fordelingen giver bedst mening.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Vælger du derimod et aktivt institut, så vil der sidde en række investorer. Deres mål er at
		holde øje med markedet, finde ud af hvor pengene bedst kan investeres imod et positivt afkast.
		Investorerne vil stadig være underlagt vedtægterne, som afgøre hvordan instituttet investere sin
		pulje.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Den normale omkostning ved foreninger ligger typisk under 1% om året, som trækkes fra din
		investerede formue. Så hvis du skyder 100 kroner ind, som er 200 kroner værd, når gebyret
		afregnes. Så trækker de op til 2 kroner ud af dine 200 kroner. Du mærker altså ikke gebyret, da
		puljen (og dertil dit investeringsbevis) falder i værdi, frem for at opkræve gebyret fra dig.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		For at give et eksempel. Hvis du investere i <a
			aria-label="Danske Invest Teknologi Indeks KL (opens in a new tab)"
			rel="noreferrer noopener nofollow"
			href="https://www.danskeinvest.dk/w/show_funds.product?p_nId=75&amp;p_nFundgroup=75&amp;p_nFund=1031"
			target="_blank"
			class="rank-math-link">Danske Invest Teknologi Indeks KL</a
		>, så betaler du 0.50% om året i administrationsgebyr. Hvis din investering i foreningen er 200
		kroner værd, så falder din investering altså 1 krone i værdi. De kommer ikke og opkræver beløbet
		fra dig.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Dertil betaler du kurtage, når du indbetaler til og trækker penge ud af en investeringsforening.
		Hvis investeringsforeningen operere i en anden valuta end DKK, så vær opmærksom på eventuelle
		vekslings gebyr.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Bonus: Start en månedsopsparing ved Nordnet</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du gerne vil investere løbende, så kan du vælge at oprette en månedsopsparing ved Nordnet.
		Her opretter du en porteføljefordeling, hvor meget du vil investere hver måned, og hvor stor
		procentdel skal indskydes hvor. Så skal du kun sørge for at overføre penge til din Nordnet
		investeringskonto, så de kan investere pengene i de valgte investeringsmål.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du bruger Nordnet's månedsopsparing, så betaler du ikke kurtage ved købet af de valgte
		investeringsmål. Du kan derfor akkumulere din portefølje helt kurtagefrit. Du skal dog betale
		kurtage når du sælger ud af din beholdning, og vekslings gebyr er stadig aktuelt ved både køb og
		salg.
	</p>
	<!-- /wp:paragraph -->

