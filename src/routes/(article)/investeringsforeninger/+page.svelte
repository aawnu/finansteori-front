		investere gennem et investeringsinstitut, som f.eks en forening eller fond. Hvis du har oprettet
		en investeringskonto, har du typisk adgang til en kategori der hedder noget i stil med
		“investeringsforeninger” eller “værdipapirfonde”.
	</p>
