<h1>Holdingselskab - 22% selskabsskat - Invester gennem egen virksomhed</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Hvis du går med en iværksætter i maven, eller gerne vil afskærme dine investeringer fra dig
		selv, så har du sikkert overvejet at investerer igennem et selskab. For mange giver det mening
		at investerer privat, da man skal tage stilling til færre ting i løbet af året. Børsmægleren
		oplyser udbytter og afkast til skat, alt efter om det er lagerbeskattet eller efter
		realisationsprincippet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:paragraph -->
	<p>
		I den her artikel tager vi et kig på at investerer igennem et selskab. Hvis du vil vide mere om
		aktieinvestering generelt, så <a
			href="/aktieinvestering"
			class="rank-math-link">læs om aktieinvestering her!</a
		> Vores eneste mål med selskabet er at investerer i værdipapirer, og ikke at producere eller sælge
		produkter. Derfor giver det mening at stifte et holdingselskab struktureret som et Anpartsselskab
		(ref. ApS).
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er interesseret i at læse mere omkring, <a
			aria-label=" (opens in a new tab)"
			rel="noreferrer noopener nofollow"
			class="rank-math-link rank-math-link"
			href="https://www.ditselskab.dk/mere-info/holdingselskab"
			target="_blank">hvad er et holding selskab</a
		>, så har advokaten Jacob Tøjner forklaret det godt i den linkede artikel. Her får du også en
		god forståelse for anvendelsen af et holdingselskab, hvis du vil starte selvstændige projekter i
		fremtiden.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad kræver det at investerer med holdingselskaber?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du vælger at vil investerer igennem et selskab, så skal du i første omgang stifte et
		selskab. Her kan du vælge at stifte et Anpartsselskab (ApS) eller et Aktieselskab (A/S), for at
		have begrænset hæftelse i tilfælde af konkurs.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du vælger at stifte et ApS skal du i 2020 have en start kapitalværdi på 40.000 kroners. Det
		kan være penge, allerede opkøbte aktier eller maskiner, som skal overdrages og anvendes i
		selskabet. Vil du derimod have et A/S, skal du have en start kapitalværdi på 400.000 kroner, og
		dertil have en bestyrelse.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når selskabet er stiftet, skal du have fat på en LEI-kode, som identificerer dit selskab på
		børsmarkedet. Det anvendes til at bekræfte at handlerne kommer fra dit selskab. Det blev indført
		som lovkrav i starten af 2018.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Flere banker og tjenester henviser dig til <a
			aria-label=" (opens in a new tab)"
			rel="noreferrer noopener"
			href="https://www.dansklei.dk/"
			target="_blank"
			class="rank-math-link">dansklei.dk</a
		>
		når du vil investerer igennem et selskab, men der findes alternative services hvis du Googler
		"Virksomhed LEI koder". <em>En Lei kode kan koste fra 260 kroner om året og op.</em>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<em
			>Hertil er det vigtigt at undersøge omkostningerne ved at have en erhvervkonto i banken.
			Eventuelt hvad en revisor koster, hvis du ikke vil sidde med regnskabet selv.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Bogføring af handler og investeringer</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Som virksomhed kan du vælge om du vil beskattes efter realisationsprincippet eller
		lagerprincippet. Her er det vigtigt at du gør op med dig selv, hvor likvide investeringer du vil
		foretage.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har planer om at lave langsigtede investeringer, kan det give mening at følge
		realisationsprincippet. Investerer du derimod i likvide og udbyttebetalende investeringer, kan
		det være en fordel med lagerprincippet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Kort opsummering:</strong> <em>Realisationsprincippet</em> betyder at man først betaler
		skat af det realiserede afkast ved salg af aktien. Herunder definerer man udbytter som direkte
		afkast, og skal indføres som indkomst til beskatning når de udbetales. <em>Lagerprincippet</em> betyder
		at man betaler en årlig beskatning af kursændringer, uanset om det er realiseret eller ej.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du køber aktier, så skal du sørge for at dokumenterer købet, antal og dets værdi. Det er
		vigtigt at vide når du enten sælger aktien eller opgøre kursændringen ved årsopgørelsen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Beskatning som selskab</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		På et tidspunkt opnår man forhåbentligt et overskud eller positivt afkast eller nogle udbytter
		fra sine investeringer. Som virksomhed opgøre man sit regnskab i slutningen af sit regnskabsår. <em
			>Det behøver ikke at følge kaldenderåret, så vær opmærksom på hvilket regnskabsår du stifter
			selskabet med.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Selskaber betaler kun selskabsskat af årets overskud, det vil sige omsætning minus udgifter. For
		at give et eksempel: Hvis du tjener 200.000 kroner, hvor af du betaler 25.000 kroner ud i
		lønninger og 5.000 kroner i kurtager. Så har du et overskud på 200.000 - 25.000 - 5.000 =
		170.000 kroner før skat. Af de 25.000 kroners løn har du betalt personlig indkomstskat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du har positivt afkast og modtager udbytter som selskab, skal indtægten medregnes i
		selskabets indtægter. Det vil sige at aktieinvestering og beskatningen ikke er adskilt i et
		selskab, ligesom hvis man investerer privat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>I 2019/2020 er selskabsskatten på 22%</strong>, men der har været snak om at sænke den
		til 19% på baggrund af Covid-19. Hvis du har investeret under loftet på en aktiesparekonto, så
		kan
		<a href="/aktiesparekonto" class="rank-math-link"
			>det være en skattemæssig fordel af udfylde aktiesparekonto</a
		> privat før man omdanner til selskab.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Vigtige paragraffer fra aktieavancebeskatningsloven</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der er meget lovgivning at tage stilling til, når man begiver sig ud i at drive selskab og
		dertil investerer i aktier. Her får du et overblik over de væsentlige paragraffer, som får dig
		igang. <strong
			>Jeg anbefaler dog at du snakker med en revisor om dine forhold, så du får et mere retvisende
			indblik.</strong
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		De følgende paragraffer er ikke den fulde lovgivning, da der er forskellige ting at tage
		stilling til. Disse paragraffer giver dog et godt indblik i, hvilke vilkår man skal forholde sig
		til, at investerer gennem selskab. <em
			>Du kan læse den fulde lovgivning her: <a
				aria-label=" (opens in a new tab)"
				class="rank-math-link rank-math-link"
				rel="noreferrer noopener"
				href="https://www.retsinformation.dk/eli/lta/2016/1148"
				target="_blank">Aktieavancebeskatningsloven</a
			></em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:quote {"className":"is-style-large"} -->
	<blockquote class="wp-block-quote is-style-large">
		<p>
			<strong>§ 5.</strong>&nbsp;Såfremt en skattepligtig ejer aktier med samme rettigheder, der er
			erhvervet på forskellige tidspunkter, anses de først erhvervede aktier for de først afståede.
		</p>
	</blockquote>
	<!-- /wp:quote -->

	<!-- wp:paragraph -->
	<p>
		Når man køber aktier og investeringsbeviser, sælger man dem i samme rækkefølge som man køber
		dem. Hvis du køber 100 aktier til 50 kroner, derefter 100 aktier til 120 kroner. Hvis du sælger
		50 aktier til 145 kroner, så sælger du halvdelen af de første 100 aktier til 50 kroner. Du får
		derved et positivt afkast på 95 kroner, og ikke 25 kroner.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:quote {"className":"is-style-large"} -->
	<blockquote class="wp-block-quote is-style-large">
		<p>
			<strong>§ 8.</strong>&nbsp;Gevinst og tab ved afståelse af datterselskabsaktier,
			koncernselskabsaktier og porteføljeaktier omfattet af § 4 C medregnes ikke ved opgørelsen af
			den skattepligtige indkomst.
		</p>
	</blockquote>
	<!-- /wp:quote -->

	<!-- wp:paragraph -->
	<p>
		Hvis man ejer mere end 10% af et selskab, så betegnes man som moderselskab, og skal ikke betale
		skat af udbytter eller ved frasalg. Hvis du laver et frasalg som får dig under 10% ejerskab, vil
		efterfølgende frasalg eller udbytter være skattepligtige, indtil du igen er på eller over 10%
		ejerskab. <em
			>Der kan dog være udlandske skatter på udbetaling af udbytter, men du beskattes ikke af det
			modtagne beløb i Danmark.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:quote {"className":"is-style-large"} -->
	<blockquote class="wp-block-quote is-style-large">
		<p>
			<strong>§ 9.</strong>&nbsp;Gevinst på aktier, der ikke er omfattet af § 8, medregnes ved
			opgørelsen af den skattepligtige indkomst, jf. dog § 10.
		</p>
	</blockquote>
	<!-- /wp:quote -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har valgt at blive beskattet efter lagerprincippet er denne paragraf ikke så vigtig at
		forholde sig til, da du betaler skat årligt, uanset om du realiserer afkastet. Har du derimod
		valgt at blive beskattet efter realisationsprincippet, skal dine udbytter og realiserede afkast
		beregnes ind i samme regnskabsår. Det bliver afgjort at dagen hvor udbytterne bliver annonceret
		og dagen hvor på aktierne sælges.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:quote {"className":"is-style-large"} -->
	<blockquote class="wp-block-quote is-style-large">
		<p>
			<strong>§ 10.</strong>&nbsp;Gevinst og tab på egne aktier medregnes ikke ved opgørelsen af den
			skattepligtige indkomst.
		</p>
	</blockquote>
	<!-- /wp:quote -->

	<!-- wp:heading -->
	<h2>Køb af aktier er ikke en udgift, kurtage og renter er</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du modtager udbytter eller sælger en aktie med positivt afkast, så er det en indkom i
		selskabet. Her skal du være opmærksom på, at pengene høre til det tilhørende regnskabsår og skal
		beskattes <em>(medmindre du ejer 10% af selskabet ved udbyttebetalingen eller frasalget)</em>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her vil mange tænke at de kan geninvesterer pengene uden at skal betale skat. Du skal ikke
		betale skat når afkastet realiseres eller udbyttet udbetales, ligesom når du investerer privat.
		Derimod skal fortjenesten bogføres og medregnes ved årsopgørelsen, hvor efter fortjenesten skal
		beskattes.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du investerer i aktier, så har du ikke en udgift, som kan trækkes fra. Ligesom når man køber
		et varelager, så laver man blot kapitalen om til en anden værdiform. Du skal derfor sørge for at
		have likvid kapital (ref. faktiske penge på kontoen) til at betale din selskabsskat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Du kan dog trække eventuelle udgifter ved købet fra, som kurtage og valutaveksling. Hvis du
		investerer i instrumenter med administrationsgebyrer eller renter, kan de også trækkes helt
		eller delvist fra.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Muligheder for dækning og udbetaling fra selskabet</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		For at opnå en god økonomisk balance, så anbefales det at man balancere udbetalinger imellem løn
		og udbytter. Hvis du opstiller selskabet rigtigt, så kan du opnå mange fordele via selskabet.
		Det kan være fri bil til rådighed med tank kort, hvor du kun skal betale den årlige beskatning
		af fri benyttelse privat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Måske har du købt en lille lejlighed, hvor du kan bo som hovedaktionær, som også betales via
		skat som fri benyttelse. Der er mange måder at opstille en fordelagtig situation ved at have et
		selskab. Du kan afskrive begge ting i selskabets regnskab, så i selskabet opbygger du en
		portefølje af aktiver imens.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Dine private udgifter og hertil skatter skal selvfølgelig dækkes. Her anbefales det at finde en
		god balance mellem udbytter fra dit selskab til dig, og eventuelle lønninger. Hvis du har
		udgifter for 16.000 kroner om måneden i beskatning af fri benyttelse af bil og bolig. Så skal du
		finde ud af hvor meget du skal bruge til mad og andre omkostninger.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her er der mange måder at opsætte udbetalinger. Det anbefales at finde en sund balance imellem
		lønninger og udbytter, da man kan optimere sine skatteforhold til ikke at opnå personlig
		topskat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Opsummering: Investering gennem selskab</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Ved at investerer igennem et selskab, så opnår du en fordelagtig adskilning mellem dine
		investeringer og dine private økonomiske forhold. Du opnår en generel lavere beskatning på de
		gevinster der ikke udbetales som løn. <em
			>Husk at der er penge at hente ved at udfylde en privat aktiesparekonto først.</em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Selve investeringen i aktier eller andre instrumenter er ikke udgifter, og du skal stadig
		beskattes af pengene. Du betaler ikke skatten ved realisering af afkastet, men ved
		årsopgørelsen. Du kan derved geninvesterer 100% af pengene, hvis du senere kan skaffe kapitalen
		til at betale skat. Det kan f.eks. være en <a
			href="/udbytteinvestering"
			class="rank-math-link">taktik ved udbytteinvestering</a
		>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du opnår et ejerskab på 10% eller mere, så er du ikke beskattet af udbytter eller frasalg
		af din investering. Det kan også gøre en nysgerrig på at investerer i små selskaber eller udvide
		med venture funding <em
			>(ref. investerer i mindre og nystartede selskaber, som mangler kapital og eventuel
			vejledning/ekspertise)</em
		>.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis man opstiller tingene rigtigt kan man opnå økonomiske fordele ved at eje ting igennem
		selskabet og nøjes med beskatning af fri benyttelse, såfremt selskabet er likvid nok til at kan
		betale lønnen til at dække disse beskatninger og dertil generelt forbrug (Eller har et arbejde
		som kan dække disse udgifter).
	</p>
