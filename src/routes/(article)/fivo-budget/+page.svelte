	<!-- wp:paragraph -->
	<p>
		Vi har en boks med 4 felter, hvor den øverste række er fordelt på faste udgifter og investering,
		og skal til sammen udgøre cirka 70% af vores indtægter. Nedenunder har vi variable udgifter og
		opsparing, som til sammen udgøre cirka 30% af vores indtægter. Det interessante ved opsætningen
		er at vi i venstre side har reserverede udgifter (altså både faste og variable udgifter) og de
		burde cirka udgøre 60% af dine indtægter hvor investering og opsparing burde cirka udgøre 40% af
		dine indtægter.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Grunden til at boksen er fordel sådan, er fordi vi læser boksen med urets retning. Så når du
		modtager vores penge, så går der et beløb til skat, det kan vi ikke ændre på. Herefter starter
		vi med at sætte penge til side til faste udgifter. Øverste række burde udgøre 70%, så hvis du
		har faste udgifter for 50%, så har du kun 20% til investering.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Derunder har vi variable udgifter og opsparing. Da vi følger urets retning, så køre vi
		højre-til-venstre, så vi afsætter 10% til vores opsparing og har resten til variable udgifter.
		Det gør vi for altid at have penge til u-forudsete udgifter. 10% bliver til 120% af dine
		indtægter over et år, så vi burde have en gennemsnitlig måneds indtægt i buffer efter et år.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Husk at have en indtægts-konto til at formidle dit budget fra</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det 5 element ligger i konto-opbygningen. Hvis du er meget disciplineret, kan du opnå det med
		mindre kontoer, men hvis du først starter ud, så skal vi have 4 bank kontoer og 1 investerings
		konto. Anbefaler at du har din investeringskonto ved Saxo Bank eller Nordnet, inden du vælger
		kan du læse artiklen <a
			class="rank-math-link"
			href="https://finansteori.dk/borshandel/nordnet-vs-saxo-bank"
			>Nordnet vs Saxo Bank, hvem er billigst?</a
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li>Indbetalinger (+ Nem-konto)</li>
		<li>Faste udgifter (+ Betalingsservice)</li>
		<li>Opsparing (evt. rente-konto?)</li>
		<li>Forbrugs konto (Variable udgifter - mad, tøj, biograf, andet)</li>
		<li>Investerings konto</li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Det absolut vigtigste ved budgetteringen er at du overføre pengene fra indbetaling til de
		respektive kontoer 1 eller 2 gange om måneden, alt efter hvor tit du får løn, og ikke bruger
		indbetalings-kontoen direkte. Der er ingen som siger at du skal investere pengene på
		investerings kontoen med det samme, du kan sagtens akkumulere op før du køber, målet er at
		reservere pengene, så du ved præcist hvad formålet er med dem.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad betyder det at betale sig selv først?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der er ikke nogen rationel god forklaring på hvad det vil sige at betale sig selv først, da man
		er den første person der modtager pengene. Selve idéen med "betal dig selv først" handler om at
		du tilsidesætte penge til dig selv før du betaler dine regninger og andet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Derfor er det vigtigt at vide hvor meget du skal sætte til side til regninger, mad og andre
		udgifter. Det hjælper ikke noget at sætte penge til siden hvis du derefter ikke kan betale dine
		regninger. Det er her vi kommer ind på at have en konto til at modtage alle vores penge på, et
		centralt sted vi kan delegere pengene fra.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du modtager løn, udbytte eller penge tilbage fra forsikring danmark, så går en forudbestemt
		del til nuværende behov og en del til at forbedre dine fremtidige vilkår. Man kan sige at du
		tager pengene ud af nuet og reservere dem til fremtidige dig, så du skal tænke mindre på at
		finde pengene til den tid.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Når du har opgjort din skat og udgifter, så tager du den totale indtægt efter skat og fordeler
		pengene ud. Hvis du oplever ikke at have nok penge til udgifter så skal du justere hvor meget du
		overføre til udgifter. På den måde kan du også se hvis udgiftkontoen går i negativ eller har et
		usædvanligt stort beløb til overs, at dine udgifter har ændret sig. Vær opmærksom på dine penge.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er livsstils-inflation?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det er meget naturligt for den ikke-økonomiske person at leve efter pengene der står på kontoen.
		Hvis man har 8.000 kroner efter skat og faste udgifter, så bruger man pengene i løbet af måneden
		på mad, byture, biografture og måske et par venne-gaver. Når man så får en forfremmelse og
		højere løn, så får man råd til en ekstra tur i byen eller måske en større bil på
		leasing-kontrakten.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Livsstils-insflation er kort og godt bare et udtryk for at man lever fra lønseddel til den
		næste. For de penge man har til overs til sig selv hver måned efter skat og faste udgifter. Man
		tænker ikke så meget på den dag man måske mister sit arbejde, pludselig skal betale en skade på
		bilen eller skal skifte et halvt køkken ud fordi de hårdehvidevarer er slidte.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Ved at føre et procent-fordelt budget som for eksempel FIVO, eller et andet, så vokser dine
		investeringer og opsparinger i relation til din eventuelle livsstils-inflation. Din økonomi kan
		derfor bedre bære at du får lidt dyrere vaner, da du har både opsparing og investering at falde
		tilbage på.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvornår giver det mening at bruge FIVO-Budgettet?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Personligt er det en formular jeg altid forsøger at følge, uanset hvor meget der kommer ind på
		min konto. Der er dog nogle forbehold som er vigtige at tage stilling til, før man begynder at
		dele sine penge op i forudbestemte grupperinger. Hvis du tjener 12.000 kroner efter skat, og har
		faste udgifter for 8.000 kroner (~67%), så giver det måske ikke mening at smide de 3% over på en
		investeringskonto, men nærmere at smide 13% (1.040 kroner) på din opsparing istedet for, så du
		stadig har råd til u-forudsete udgifter.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er i en økonomisk situation, hvor du har mere brug for at fokusere på din nuværende
		situation, end din potentielle fremtidige situation. Så giver det typisk bedre mening at
		forholde sig til en opsparing frem for at investere.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Du kan selvfølgelig vælge at investere en del af din opsparing, så længe du er opmærksom på at
		markedet kan falde i værdi og at du skal betale skat af dine fortjenester. Du vil havne i en
		situation hvis du forventer at have 50.000 kroner på din opsparing, men mangler at betale skat
		af 20.000 af dem.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Opsummering, sådan kommer du igang med FIVO budgettet</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er interesseret i at have et utroligt simpelt og "dummy"-sikkert budget, så kan FIVO
		eller et ligende budget hjælpe dig igang med at håndtere dine penge. Formålet med selve FIVO er
		at opdele pengene ud fra at månedligt perspektiv, og ikke direkte af indtægterne. Hvis du har
		variable indtægter, så kan du vælge at overføre ekstra til enten investering, opsparing eller
		variable omkostninger.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Den måde jeg selv skiftede over var meget hård og kontant, da jeg skiftede gik direkte igang med
		uddelingerne på gennemsnitlig vis. Når jeg så havde højere udgifter den en måned, så tog jeg fra
		variable udgifter og købte lidt mere simpel mad i den måned. Hvis du har et meget stramt mad
		budget i forvejen, så skal du måske investere eller spare lidt mindre op i de måneder.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Personligt går jeg ind på kontoen omkring den 25 i måneden, da jeg fik løn den dag i sverige.
		Her opstiller jeg dato-specifikke overførelser til den 1. i måneden efter, uanset om det er
		hverdag eller weekend. Selve overførelsen skal banken nok sørge for sker på et passende
		tidspunkt deromkring.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Jeg logger ind og sætter min indtægtskonto til at overføre til de andre kontoer ud fra tallet
		mit budget siger. Hvis der stadig er penge i overskud når pengene er overført, så gik de ind på
		min opsparingskonto, da jeg fortrækker minimum 3-6 måneders faste + variable udgifter til
		nødsituationer uden job, sygdom eller andet.
	</p>
	<!-- /wp:paragraph -->

