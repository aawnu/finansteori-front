	<!-- wp:heading -->
	<h2>Udgiftskilder, hvem har reservering på dine indtægter?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det er næsten umuligt at undgå udgifter i den moderne verden, da vi alle skal have et sted at
		bo, gerne med el, vand og varme. Dertil vil vi gerne have noget mad, internet og tøj. Nogle
		udgifter er dog vigtigere end andre, derfor har vi fordelt dem i forholdsvist faste udgifter og
		variable udgifter.
	</p>
	<!-- /wp:paragraph -->

