	<!-- wp:heading -->
	<h2>De grundlæggende elementer bag FIVO budgettering</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		FIVO lyder næsten som "five", altså 5 på engelsk, og så dækker det over de 4 aspekter jeg går
		efter på dansk. Formålet med FIVO er hver måned at have penge til <strong>F</strong>aste
		udgifter, <strong>I</strong>nvesteringer, <strong>V</strong>ariable udgifter og ikke mindst
		<strong>O</strong>psparing. Det er her det begynder at blive lidt avanceret at forklare, men
		simpelt at bruge, når man forstår konceptet.
	</p>
	<!-- /wp:paragraph -->

