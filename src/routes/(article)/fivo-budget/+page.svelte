	<!-- wp:paragraph -->
	<p>
		Her har du et par mulige kategorier, som kan være potentielle indtægtskilder. Vil anbefale at du
		sætter dig ned med din bank konto og tjekker hvor meget du gennemsnitligt har fået fra de
		forskellige kilder, over de seneste 6-12 måneder. Du skal kun kigge på pålidelige indtægter, og
		ikke medtage midlertidige indtægter, bonusser eller udbetalinger fra forsikringer.
	</p>
