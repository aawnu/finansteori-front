	<h2>Indtægtskilder, hvor kommer pengene fra?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der er mange former for indtægtskilder, men de 3 typiske indtægtskilder er løn, a-kasse eller
		kontanthjælp. Det er specielt gældende hvis folk ikke har nogen afkastende investeringer. Først
		skal vi have fundet alle dine indtægtskilder, altså hvor kommer dine indtægter fra. Derefter
		skal vi opgøre hvor meget vi gennemsnitligt kan forvente fra den indtægtskilde.
	</p>
	<!-- /wp:paragraph -->

