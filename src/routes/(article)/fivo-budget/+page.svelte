	<p>
		På listen finder du nogen af de helt basale udgiftskilder, som man typisk skal have betalt
		månedligt. På listen har jeg undladet at skrive <strong>mad</strong>, da det høre under basal
		overlevelse. Her anbefaler jeg igen at du tjekker dine gennemsnitlige udgifter over seneste 6-12
		måneder, så du ved cirka hvor meget du skal lægge til side hver måned. På den måde udligner du
		dine månedlige udgifter istedet for at du har ekstra høje udgifter hver 3 eller 6 måned når
		forsikringerne skal betales.
	</p>
