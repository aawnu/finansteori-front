	<!-- wp:heading -->
	<h2><strong>Hvad så med skat og alt det andet med småt?</strong></h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		En af de andre grunde til at vi har fået oprettet både en børneopsparing og en almindelig konto,
		skyldes, at der er forskellige skatteregler på disse to. På børneopsparingen skal man IKKE
		betale skat af afkastet på nuværende tidspunkt, hvis du investerer pengene. Det er også derfor,
		at der er et max på 72.000 kroner. Vær dog opmærksom på at der altid er nogle gebyr, når du
		handler med finansielle midler.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		På den almindelige konto gælder der nogle lidt andre skatteregler. Indbetaler du som forældre
		til den almindelig konto, og investerer pengene, så bliver du som forældre beskattet af
		afkastet. Og det er ligegyldigt, om det er aktier, obligationer, ETF’er, akkumulerende
		investeringsforeninger osv. du investerer i.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis familiemedlemmer såsom fx bedsteforældre indbetaler til vores søns almindelige konto i
		forbindelse med en pengegave, og vi som forældre investerer pengene på barnets vegne i fx.
		enkeltaktier, så er det barnet selv, der bliver beskattet af afkastet. Barnet har dog et
		personfradrag på 36.100 (under 18 år) kroner om året, som det kan gøre brug af, hvis
		pengegaverne fra familiemedlemmer bliver investeret i noget, som bliver beskattet som
		kapitalindkomst, hvilket for eksempel kunne være en akkumulerende investeringsforening, mens
		investering i aktier realisationsbeskattes, hvilket betyder, at barnet skal betale skat af en
		gevinst på det tidspunkt, hvor aktien sælges.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2><strong>Tiden er din ven</strong></h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Som jeg tidligere har nævnt, så er tiden og renters rente effekten din bedste ven i den her
		sammenhæng. En børneopsparing står ofte i rigtig lang tid, hvilket giver de optimale forhold til
		at få pengene til at yngle. De 72.000 kroner kan altså gå hen og blive til rigtig rigtig mange
		penge, hvis du har fokus på at få dem indbetalt og investeret hurtigst muligt.&nbsp;
	</p>
