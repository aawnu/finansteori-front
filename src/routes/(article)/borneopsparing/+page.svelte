<h1>Børneopsparingen: Sådan får du pengene til at yngle</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		<em
			>Artiklen er skrevet af Caroline Johansen, kvinden bag Tradies<s>.dk</s>. Jeg fortæller i
			denne artikel om, hvordan du kan få dine børns børneopsparing til at vokse.
		</em>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2><strong>Lidt baggrundsinfo</strong></h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Jeg er hverken finansiel rådgiver, ekspert eller har en fin økonomisk uddannelse bag mig. I
		virkeligheden er jeg nok mere den kreative type, der altid har hadet matematik og tal. Jeg har
		til gengæld en stor interesse i at skabe økonomisk frihed for mig selv og min familie. Og det
		tror jeg faktisk, de fleste har. Desværre kan det der med investeringer godt sommetider blive
		omtalt som et område, som kun er forbeholdt mænd i jakkesæt og med spidse sko.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Spoiler alert. Det er det ikke! Investeringer og økonomi er forbeholdt alle, der har lyst til at
		dykke ned i det. Heldigvis har vi i dag alle lige muligheder for at tage ansvar for vores egen
		økonomi. Og det er ligegyldigt, om du er mand eller kvinde, rig eller fattig, ung eller gammel.
		I virkeligheden handler det måske mere om at gøre det her med investeringer og økonomi mere
		spiseligt for helt almindelige mennesker som du og jeg.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Denne artikel er skrevet ud fra egne erfaringer. Jeg vil desuden altid anbefale, at man
		forholder sig kritisk, og følger sin egen intuition i penge- og investeringsverdenen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2><strong>Giv dit barn en god start på livet</strong></h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Kort tid efter at jeg fødte vores søn, oprettede min kæreste og jeg en børneopsparing til ham
		med dertilhørende aktiedepot. Det gjorde vi fordi, at vi allerede inden hans ankomst havde
		snakket en masse om, hvordan vi med relativt ganske få midler kan give ham en god start på hans
		voksne liv. I know - det ligger langt ude i fremtiden, når man sidder der med en lille nyfødt i
		armene uden manual. Og det kan helt ærligt også virke en smule uoverskueligt at tænke på, at ens
		hjælpeløse lille klump kød en dag vokser op, og skal tage kørekort og købe fast ejendom. Men det
		er nu engang sådan, at verden hænger sammen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Helt lavpraktisk ringede vi til vores bankmand og sagde “Dav, vil du oprette en børneopsparing
		plus en helt almindelig konto til Junior? Og vi vil gerne bede om at få tilknyttet et aktiedepot
		til både børneopsparingen og den almindelige konto.”&nbsp;
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Vi valgte at oprette en børneopsparing samt en helt almindelig konto (også kaldet en Oskarkonto
		i Jyske bank) i samme omgang, fordi det giver mest mening for os i forhold til investeringer af
		hans penge. I 2020 kan man “kun” indbetale 6000 kroner om året på børneopsparingen og 72.000
		kroner i alt. Det betyder for os, at vi som forældre har tænkt os at indbetale 6000 kroner én
		gang om året de næste 12 år, hvorefter vi investerer dem med det samme. Når de 12 år er gået, og
		vi har nået max på de 72.000 kroner, har pengene yderligere 9 år til at yngle i, da vores søn
		først kan få sin børneopsparing udbetalt, når han bliver 21 år.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Grunden til at vi har så “travlt” med at indbetale pengene på hans børneopsparing, er, at
		renters rente effekten allerede træder i kraft, så snart de første penge er investeret. Det fede
		ved en børneopsparing er, at pengene ofte står i lang tid. Og tid og renters rente effekten er
		din bedste ven, når du investerer!
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Som førnævnt fik vi også oprettet en helt almindelig konto til Junior. Det gjorde vi fordi, at
		vi har valgt at børneopsparingen, er en konto, hvor kun vi som forældre indbetaler til. Den
		anden konto er derfor tiltænkt til de beløb og pengegaver, vores søn kommer til at få i løbet af
		hans barndom og teenageår. Blandt andet pengegaverne fra hans barnedåb er sat ind på hans
		Oskarkonto (eller bedsteforældrekontoen, som vi også kalder den).
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2><strong>Hvorfor overhovedet investere på barnets vegne?</strong></h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		At få tilknyttet et aktiedepot til børneopsparingen og/eller barnets almindelige konto betyder
		blot, at du har mulighed for at købe og sælge aktier og obligationer osv. på barnets vegne. Og
		hvorfor så overhovedet investere de opsparede penge? Det er der to rigtig gode grunde til:
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Den ene grund hedder inflation. Lader du blot pengene stå passive på en konto i flere år, bliver
		de mindre værd. Lad mig vise dig et lille regnestykke, som beskriver det meget godt:&nbsp;
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Har du sparet de 72.000 kroner op, som børneopsparingen tillader, og lader dem stå passive i en
		periode på 10 år, altså uden at investere dem, så vil pengene være 59.065 kroner værd, da man
		regner med, at priserne på blandt andet helt almindelige dagligvarer stiger i gennemsnit med ca.
		2% procent hvert år. Den stigning kaldes også for inflation. Du vil altså stadig have 72.000
		kroner stående på kontoen (medmindre du skal betale negativ rente af at have penge stående, som
		man skal i mange banker på nuværende tidspunkt). Men i og med at priserne på blandt andet
		dagligvarer stiger, så vil dine 72.000 kroner være mindre værd, da en liter mælk som koster 10
		kroner i dag, vil koste dig 12,5 i stedet om 10 år.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Og det er her, at den anden gode grund kommer ind i billedet. Investering af pengene. Lad os
		sige at du i stedet investerer de 72.000 kroner i fx aktier eller i en investeringsforening. Og
		lad os gå ud fra at du på en periode på 10 år har et gennemsnitligt årligt afkast på 3%
		fratrukket inflationen. Så vil du i stedet stå tilbage med 83.880 kroner (beregningen tager ikke
		højde for omkostninger og skat). Det virker da noget mere attraktivt, hvis du spørger mig!
		Selvfølgelig er der risiko for, at dine investeringer går op og ned i løbet af årene. Men alt
		andet lige er det bedre at investere pengene end bare at lade dem stå passive.
	</p>
	<!-- /wp:paragraph -->

