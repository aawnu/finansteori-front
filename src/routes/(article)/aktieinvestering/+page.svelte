<h1>Hvad er Aktieinvestering? Alt om aktier for begyndere, start din portefølje i dag!</h1>

<div class="wp-article">
	<p>
		Aktieinvestering er en god måde at akkumulere en formue eller opsparing over tid. På den måde
		sætter du dine penge i arbejde, istedet for at inflation formindsker din formue på en bank konto
		uden positiv indlånsrente. Der er flere måder at investere i aktier på, ud over at købe dem
		direkte, kan du også investere i investeringsforeninger eller ETF'er.
	</p>

	<h2 id="hvad-er-aktier">Hvad er aktier?</h2>

	<p>
		Aktier er et værdipapirer, der giver ejerskab over en andel af en virksomhed, forening, fond
		eller gæld. Når du køber aktier i en virksomhed, betyder det at du køber en andel af
		virksomheden, og bliver medejer. Dit ejerskab er svarende til antal aktier du ejer, ud af det
		totale antal aktier på markedet.
	</p>

	<h2 id="hvor-kommer-aktier-fra">Hvor kommer aktier fra?</h2>

	<p>
		I Danmark er det kun aktieselskaber, som kan blive listet på en aktiebørs. Et aktieselskab går
		med forkortelsen A/S, og er en selvstændig juridisk enhed. Det betyder at selskabet fungere med
		sin egen "person", ligesom du har dit CPR-nummer, så har virksomheden et CVR-nummer.
	</p>

	<p>
		Når man stifter et aktieselskab, så bliver det oprettet i aktier, som bliver fordelt mellem
		ejerne. Det er igennem disse aktier, at du beviser ejerskab over den del af selskabet.
	</p>

	<p>
		I 2020 koster det 400.000 kroner at stifte et aktieselskab, så for at fordele økonomien ud,
		opretter man typisk 400.000, 800.000 eller 1.200.000 aktier, så det er nemt at fordele
		ejerskabet ud på indskudt kapital. Hvis selskabet stiftes med 800.000 aktier, og du indskyder
		160.000 kroners egenkapital, svarende til 40% af 400.000 kroner, så modtager du 320.000 aktier.
	</p>

	<p>
		Man kan senere vælge at søge investorer, som vil skyde penge ind i virksomheden, tage et lån,
		eller opsøge kapital ved at børsnotere virksomheden. Herunder ser du 2 typiske grunde til, at en
		virksomhed bliver børsnoteret.
	</p>

	<h3>1) Kapitalfremskaffelse</h3>

	<p>
		Virksomheden når et punkt i sin udvikling, hvor de har brug for penge til at fortsætte. Det kan
		f.eks. være til at udvide forretningen. Det giver mening for virksomheden, at oprette nye
		andele, med formål at sælge ejerskab i virksomheden på en aktiebørs.
	</p>

	<p>
		Det betyder at når du køber de nye andele, så modtager virksomheden dine penge som brugbar
		kapital. Som første køber af aktien, er du virksomhedens investor, der skyder kapital ind i
		virksomhed. Du kan efterfølgende sælge aktierne videre, hvor gevinsten eller tabet er dit.
	</p>

	<h3>2) EXIT-Strategi</h3>

	<p>
		Hvis der kommer en investor, som skyder kapital ind i virksomheden, så køber han ikke andele fra
		stifterne. I stedet for, så opretter man nye andele, baseret på værdisættelsen, så pengene
		havner i selskabet selv. Hvis investoren vil indskyde 200.000 for 25% af virksomheden, så
		beregner man hvor mange andele der skal oprettes.
	</p>

	<p>
		Hvis du har set tv-programmet Løvens Hule, så er du bekendt med værdiansættelser af
		virksomheder. Processen hvor de forhandler overtagelses andel af virksomheden de køber, og hvor
		meget virksomheden skal være værd til den investering.
	</p>

	<p>
		Når virksomheden er blevet moden og forhåbentligt mange flere penge værd, vil investoren
		realisere sin investering i virksomheden. Det kan ske ved at børsnotere selskabet, hvor løven
		(ref. investoren) sælger sine andele til dig og mig.
	</p>

	<p>
		<em
			>En eksempel på en velkendt exit-strategi i Danmark, er da Jesper Buch solgte sine andele af
			Just-Eat.</em
		>
	</p>

	<h2>Hvad er forskellen på A og B aktier?</h2>

	<p>
		Når en virksomhed lader sig børsnotere, kan det vælge at have flere aktieklasser. Typisk ser man
		A-aktier og B-aktier, men der kan også forekomme C-aktier i særtilfælde.
	</p>

	<p>
		Når du køber aktier i et selskab, så opkøber du en andel af selskabet, uanset om det er A-aktier
		eller B-aktier. Hvis der totalt er 150.000 aktier, og du køber 10.000 aktier, så opkøber du
		6.67% af virksomheden, uanset om du har A, B eller blandede aktier.
	</p>

	<p>
		Forskellen på de 2 aktietyper er hvor stor stemmeret du har. Normalt er A-aktien hovedaktien,
		som har stemmeret, men i nogle tilfælde har det også været B-aktien. Den modsvarende aktie vil
		derved typisk have lavere eller ingen stemmeret.
	</p>

	<p>
		<strong>FAKTA:</strong>
		<em
			>I aktieselskabsloven §67 var det bestemt, at forskellen på aktieklasser højest måtte udgøre
			10:1, hvilket betød at B-aktier som minimum havde 10% stemmeret i forhold til A-aktier. Denne
			lov blev udfaset i 2010, da vi overgik til selskabsloven. Danske virksomheder kan derfor også
			have stemmeløse aktieklasser i dag.</em
		>
	</p>

