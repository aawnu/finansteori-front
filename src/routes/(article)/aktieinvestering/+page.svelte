	</p>

	<p>
		Hvis du vælger at investere i udbytteaktier, skal du være opmærksom på hvordan udbytteprocenten
		fungere. Når du ser på en virksomhed, vil der være en udbytteprocent, som indikere hvor stort
		udbytte du får årligt. Den indikation er baseret på sidste års udbytte, imod nuværende
		handelspris.
	</p>

	<p>
		Hvis en aktie koster 70 kroner, og har en udbytteprocent på 11%, betyder at du kan forvente at
		modtage 11% af 70 kroner i udbytte om året, før skat. Hvis aktien stiger til 85 kroner, vil
		udbytteprocenten falde til 9%. Du kan derfor kun bruge udbytteprocenten, som en forventet
