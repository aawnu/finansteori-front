	<h2 id="hvad-koster-det-at-handle-aktier">Hvad koster det at handle aktier?</h2>

	<p>
		Når du har fundet de virksomheder du vil investere i, er det tid til at handle deres aktier. Her
		kan du vælge at købe aktien til nuværende salgspris. Du kan også lave et tidsbegrænset
		købstilbud, hvor den automatisk køber aktien til dit tilbud, hvis muligt.
	</p>

	<p>
		Når handlen bliver gennemført, bliver der pålagt et handels-gebyr af banken, som i
		investeringsverdenen bliver kaldt for Kurtage. Kurtagen er typisk en procent af handlens værdi,
		som bliver lagt oven på handelsbeløbet. Hvis du køber eller sælger aktier for 5.000 kroner, og
		kurtagen er 0.10%, så betaler du 5.000 + 0.10% for at udføre handlen.
	</p>

	<p>
		Det er vigtigt at undersøge dine kurtage-priser, så du har et indblik i hvad det koster dig at
		handle. For at give et eksempel, så tager Nordnet 0.10% minimum 29 kroner per handel, hvis du
		har en standardaftale.
	</p>

	<h2>Udlandske aktier og dens valuta</h2>

	<p>
		Hvis du vælger at investerer i udlandske aktier, så investerer du også i deres møntfod. Køber du
		svenske aktie, så veksler du først til svenske kroner og så køber du aktien til dens svenske
		pris. Det betyder at du kan miste penge, selv om aktien stiger i værdi, ved at valutaen bliver
		mindre værd. Omvendt kan vi også udligne et tab i aktien, hvis valutaen bliver mere værd.
	</p>

	<p>
		Valutaveksling fungere ved at man først kigger på købs og salgsprisen, hvor hovedvalutaen er
		opgjort som index 100. Det betyder at hvis vi vil købe svenske kroner, så vil opgøre vi hvor
		meget 100 svenske kroner er værd i danske kroner.
	</p>

	<p>
		Det kan f.eks set ud sådan her: SEK/DKK 71.32, som betyder at 100 SEK bliver lavet om til 71.32
		DKK. Her kan vi bruge det som en procent-sats. Hvis vi skal bruge 5.000 SEK, så kan vi beregne
		vores danske pris uden vekslingsgebyr: 5.000*0,7132= 3.566 DKK.
	</p>

	<p>
		Stiger kursen på den svenske kroner, altså bliver mere værd i forhold til den danske kroner,
		imens vores penge står i svenske aktier. Så vil vores investering falde tilsvarende i værdi, når
		vi sælger aktierne og får danske kroner retur.
	</p>

	<h2>Hvad bestemmer kursen på aktier?</h2>

	<p>
		Aktier er den reneste version af udbud og efterspørgsel, med en reference til virksomhedens
		indre værdi og fremtidige udsigter og forventninger. Det vil sige at aktiekursen (ref.
		handelsværdien) er udelukkende baseret på hvad folk er villig til at købe og sælge sine aktier
		for.
	</p>

	<p>
		Aktiekursen vil stige og falde i takt med hvad den generelle befolkning værdisætter
		virksomheden. Hvis folk vil af med deres aktier, men der ikke er nogen købere, vil det ofte
		drive kursen ned i pris. Omvendt, hvis mange er interesseret i at købe, men folk ikke vil sælge,
		så vil kursen stige.
	</p>

	<p>
		Prisen kan også blive påvirket af eksterne faktorer, som f.eks Covid-19, hvor folk er bange for
		om virksomheden kan overleve. Så vil det for nogle være vigtigt at formindske tabet, ved at
		sælge aktierne lidt billigere, for at komme af med dem hurtigt.
	</p>

	<h2>Hvad er værdihandel?</h2>

	<p>
		Hvis du kigger på investeringsselskaber, så er der typisk 2 hovedstrategier. Den ene kalder man
		værdi handel, fordi man handler aktier i speculation om at aktien stiger eller falder i værdi.
		Formålet er altså at finde aktier der sælges under deres fremtidig værdi, så de kan sælge dem
		når de er mere værd.
	</p>

	<p>
		Omvendt kan man også spekulere i at aktien vil falde, og derved 'Short' aktien. I det her opslag
		kommer vi ikke mere ind på shorting, da man kan tabe flere penge end man har "investeret", i
		modsætning til at købe en aktie der falder i værdi.
	</p>

	<h2>Hvad er udbyttehandel?</h2>

	<p>
		Når en virksomhed er moden nok til at finansiere sig selv og fremtidige udvidelser, så vil der
		typisk begynde at komme udbytter. Det betyder at virksomheden tager en del af sit overskud,
		efter skat, og udbetaler det til virksomhedens ejere (ref. investorer).
	</p>

	<p>
		Du kan tage et kig på Danske Bank, som ikke længere har vokseværk, men derimod er modnet og kan
		holde sig selv kørende med stabilt overskud hvert år. De bestræber at udbetale 40-60% af sit
		overskud til sine investorer.
	</p>

	<p>
		Kilde: <a
