	<h2>Hvad er FIFO-Princippet?</h2>

	<p>
		I Danmark har man valgt at indføre FIFO-Princippet, som betyder "First in - First out". På den
		måde sælger du dine aktier i samme rækkefølge som du køber dem. Her er et eksempel:
	</p>

	<!-- wp:list -->
	<ul>
		<li>I Januar køber du 100 aktier (A) for 83 kroner</li>
		<li>I Februar køber du yderligere 50 aktier (B) for 110 kroner</li>
		<li>I Marts vælger du at sælge 50 aktier til 235 kroner</li>
	</ul>
	<!-- /wp:list -->

	<p>
		På grund af FIFO-Princippet, betyder at du sælger dine første 50 (A) aktier. Du har derfor en
		realiseret gevinst på (235-83=) 152 kroner per aktie før kurtage og beskatning.
	</p>

