	<h2>Hvad er risikoen ved aktieinvestering?</h2>

	<p>
		Når du investerer dine penge i aktier, er der altid en risiko for at værdipapiret (ref. aktien)
		kan gå ned i værdi. Det betyder at hvis du vælger at sælge aktien videre, vil du have et tab.
		Dertil er der også en risiko for at virksomheden bag aktien kan gå konkurs.
	</p>

	<p>
		Din største risiko er altså, ved normal aktieinvestering, at du kan miste de penge som er
		investeret i aktien. Derfor anbefaler man altid ikke at investerer penge, som du kan undvære, i
		tilfælde at investeringen formindskes eller går tabt.
	</p>

	<h2>Alternativt: Invester i fonde</h2>

	<p>
		Hvis du ikke er klar til at investere direkte i aktier, kan du vælge at skyde penge ind i en
		investeringsfond. Her fra vil fonden investere dine penge ud fra forudbestemte kriterier og
		retningslinjer. På den måde kan du investere i lande, råvarer eller brancher ved at vælge
		forskellige fonde.
	</p>

	<p>
		Når du investere inde penge i en investeringsfond, er det typisk en åben fond. Det betyder at de
		opretter nye værdipapirer når du investere penge i dem, så du kan bevise hvor stor andel af
		fondens økonomi, du ejer.
	</p>

	<p>
		Det er meget normalt for en fond at have 20-25% likviditet, hvilket betyder at de kun investere
		75-80% af dine penge. Det er nødvendigt, for at de hurtigt kan betale dig tilbage, når du en dag
		vil have pengene ud af fonden igen.
	</p>

	<p>
		Bliv klogere på at <a
			href="https://finansteori.dk/borshandel/investere-gennem-investeringsinstitutter"
			>investerer gennem investeringsinstitutter her!</a
		>
	</p>

	<h2>Alternativt: Invester i ETF'er</h2>

	<p>
		Du kan også vælge at investere i ETF'er, hvilket er kort for "Exchange-Traded Funds". Det
		betyder at fonden har et begrænset antal andele, som du køber fra andre investorer, ligesom
		aktier. Fonden modtager derfor ikke dine penge, og anvender kun deres egenkapital til at
		investerer.
	</p>

	<p>
		Du skal dog være opmærksom på, at ETF'er ikke er beskattet ud fra realiseret afkast. Du vil
		istedet for blive beskattet efter lagerbeskatnings-princippet. Skattesatserne er stadig de
		samme, som ved normal aktieindkomst. Beregningen af skatten er opgjort anderledes.
	</p>

