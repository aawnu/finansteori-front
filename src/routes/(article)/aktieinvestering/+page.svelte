			rel="noreferrer noopener nofollow">Skat: C.B.2.1.1.14 Forklaring af lagerbeskatning</a
		>
	</p>

	<h2>Opsummering: Hvorfor burde jeg investere i aktier?</h2>

	<p>
		Det gør ikke den store forskel om du har en stor formue, eller om du lægger en smule fra hver
		måned. Formålet med aktieinvestering, er at forbedre sin økonomiske situation gradvist, så man
		overkomme inflation og forhåbentligt formere sine penge.
	</p>

	<p>
		Ved at investere i aktier, så investere du i en virksomhed. Du skal derfor finde en virksomhed,
		som du tror på, vil klare sig godt i fremtiden. Det er blandt andet derfor, at Warren Buffett
		klarer sig så godt.
	</p>

