	<h3>Hvordan fungere "X dagen" for udbytter?</h3>

	<p>
		Når en virksomhed opgører årets overskud, kan de vælge at udbetale en del af overskuddet i
		udbytte. Det fungere ved at direktionen har en generalforsamling, hvor de afgøre hvor meget der
		skal udbetales og hvornår.
	</p>

	<p>
		Når de har valgt en dato for udbetaling, fastsættes der også en X-Dag, som er dagen de fastgøre
		hvilke ejere der er berettiget til udbyttet. Hvis udbyttet bliver udbetalt den 30 August, vil
		X-Dagen typisk være 3-5 dage forinden, f.eks. den 26 August. Det betyder at du skal eje aktien
		den 26 August for at være berettiget til udbetalingen den 30 August.
	</p>

	<p>
		Du kan derfor købe aktien den 25 August, blive berettiget til udbytte den 26 August, og sælge
		aktien igen den 27 August, og stadig modtage udbyttet. Det ville dog ikke give mening, da
		aktiekursen falder i takt med at virksomhedens indre værdi falder tilsvarende udbyttebetalingen.
	</p>

	<h2>Hvad betyder udtrykkende EPS, P/E og P/B?</h2>

	<p>
		Der er mange fagord og udtryk at forholde sig til, når man handler med aktier. Her får du en
		kort gennemgang af de 3 mest almindeligt brugte ord, så du kan afgøre om du vil bruge dem i dine
		investerings analyser. Her kommer vi til at dække EPS, P/E og P/B.
	</p>

	<h3>P/E står for "Price on Earnings"</h3>

	<p>
		Når en virksomhed tjener penge, så er der et overskud. Her beregner man årsresultatet imod hvad
		aktiekursen er lige nu. Der er både en historisk og en spekulativ udgave af P/E værdien. Den
		historiske viser hvad vi betaler per krones overskud ved sidste årsresultat. Den spekulative
		viser os hvad vi betaler for den forventede kommende årsresultat.
	</p>

	<p>
		For at give et eksempel, kan vi kigge på den historiske formular. Hvis et selskab har 100.000
		aktier, og sidste år havde et resultat på 430.000 kroner, så ville vi have en fortjeneste på <em
			>430.000/100.000=</em
		>
		4,30 kroner per aktie (også kaldet "Earnings per share" <strong>[EPS]</strong>). Hvis aktien
		koster 28 kroner, så vil vi altså betale <em>28/4,30=</em> 6,51 kroner per tjente krone sidste år,
		efter skat.
	</p>

	<h3>P/B står for "Price on Book-value"</h3>

	<p>
		En virksomheds bogføring er bygget op af aktiver og passiver, som viser hvor meget et selskab
		har i værdier i forhold til udgifter og gæld. Ved at tage selskabets aktiver og trække
		passiverne fra, så ender du med egenkapitalen. Her fra deler du egenkapitalen ud på alle
		selskabets udstedte aktier.
	</p>

	<p>
		Hvis selskabet har en egenkapital på 7.000.000 kroner, fordelt på 1.000.000 aktier, giver 7
		kroners egenkapital per aktie. Hvis aktien koster 28 kroner, så betaler du <em>28/7=</em> 4 kroner
		per indre krones værdi i selskabet.
	</p>

	<h2>Hvordan beskattes mine aktieinvesteringer som privat?</h2>

	<p>
		Danmark har en progressionsgrænse for private, hvor man i 2020 betaler 27% af de første 55.600
		kroner, og 42% af resten derover. Dine aktieinvesteringer har en særskilt beskatning, så din
		beskatninger og fradrag er kun fordel ud over dine aktieinvesteringer.
	</p>

	<p>
		Du bliver kun beskattet af realiseret gevinst, hvilket betyder at du kun beskattes af udbytter
		og fortjenesten fra at sælge en aktie. Du kan derimod modregne skatten, via skattefradrag, når
		du realisere et tab.
	</p>

	<p>
		Hvis du køber en aktie til 100 kroner og senere sælger den for 200 kroner, så har du en
		realiseret gevinst på 100 kroner, som skal beskattes. Var aktien derimod faldet til 65 kroner,
		ville du kunne trække dit realiserede tab på 35 kroner fra fremtidige gevinster og udbytter.
	</p>

