<h1>Bull og Bear Certifikater - Tjen penge på aktier uden af eje dem</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		BULL og BEAR certifikater gør det muligt at spekulerer i kursændringer, uden at eje det
		underlæggende værdipapir. Normalt vil man købe aktier, når man forventer at kursen går op, og
		dertil short aktierne, hvis man forventer at kursen falder.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:paragraph -->
	<p>
		<a href="https://finansteori.dk/borshandel/hvad-er-shorting-af-aktier" class="rank-math-link"
			>Det kan være risikabelt at short aktier</a
		>, da man kan risikerer at miste mange penge. Der er ikke nogen begrænsning for hvor meget
		aktier kan stige i værdi, og dertil ikke nogen aktuel grænse for hvor dyrt en shorting position
		kan blive.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er BULL og BEAR certifikater?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		BULL og BEAR certifikater er et udtryk for "Constant Leverage Certificates", også kaldet
		"Contract for Different" (ref. <strong>CFD</strong>'er). Det betyder at man spekulerer med eller
		mod markedet, uden at købe eller short-sell det underliggende værdipapir. Her er det vigtigt at
		forholde sig til om certifikatet følger BULL eller BEAR markedet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>BULL certifikater</strong> følger markedets positive afkast. Hvis du køber et BULL Certifikat,
		så tjener du penge på samme måde, som hvis du købte værdipapiret. Køber du et BULL certifikat med
		gearing (ref. med indbygget lån), så tjener og taber du X gange kursændringen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>BEAR certifikater</strong> spejler markedets afkast. Hvis du køber et BEAR certifikat, så
		tjener du penge når værdipapiret falder i værdi. Certifikatet går altså den modsatte vej af det underlæggende
		værdipapir. Hvis du har gearing på et BEAR certifikat, så tjener du flere penge ved kursfald, og
		taber hurtigere penge ved kursstigninger.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan læser man certifikatets navn?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er interesseret i at komme igang med BULL og BEAR Certifikater, så er det et par ting du
		skal forstå først. Når du åbner listen over udstedte certifikater, så vil du se en række
		forskellige navne, som f.eks:
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li>BEAR DAX X5 NORDNET</li>
		<li>BEAR SPX X15S NORDNET D</li>
		<li>BULL OLIE X8 NORDNET D6</li>
		<li>BULL PANDORA X4S NORDNET D</li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Navnet er opstillet som " Market, Underliggende værdipapir, Gearing, Udbyder". Så hvis vi kigger
		på det første eksempel. <strong>BEAR</strong> betyder at papiret stiger i værdi når kursen
		falder. <strong>DAX</strong> referer til DAX Indekset. <strong>X5</strong> betyder at du har en
		gearing på 5 gange. Når du køber 1 værdipapir, så ændres værdien sig, som hvis du havde 5
		værdipapirer. Hvis BEAR aktien falder 1 krone, så stiger din værdi 5 kroner eller omvendt.
		<strong>NORDNET</strong> er udbyderen af BEAR Certifikatet, i det her tilfælde vil det være Nordea,
		som har udgivet certifikatet for Nordnet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<strong>Kort opsummering:</strong> <strong>BEAR</strong> stiger når værdipapiret falder og
		omvendt. <strong>DAX</strong> er indekset, som du spekulerer imod. <strong>X5</strong> er hvor
		stor gearingen er. <strong>NORDNET</strong> er udbyderen, i det her tilfælde Nordea, som er partnerbanken
		af disse certifikater.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Ser vi derimod på den sidste, kan du se at den hedder <strong>X4S</strong> ved gearingen. Det
		betyder at papiret er udgivet med en STOP-LOSS funktion. Det betyder at der hver dag sættes en
		aktiv begrænsning på hvor meget certifikatet på falde i værdi, før det automatisk bliver indløst
		for at hjælpe med at forhindre tab.
		<em>Stop-loss er dog ikke en garanti for at du ikke taber din fulde investering.</em>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan fungerer gearing?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du køber et BULL Certifikat med X5 gearing, så vil du modtage svarende til 5 værdipapirer,
		når du køber 1 certifikat. Hvis værdipapiret ligger på 100 kroner, så vil du låne 400 kroner og
		opnå svarende til 5 værdipapirer. Her er det vigtigt at huske at du har lånt 400 kroner, når du
		skal kigge på gevinst og tab.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Ligesom alle andre lån, så er der et administrationsgebyr og dertil renter. Her skal du være
		opmærksom på at renten opgøres dagligt. Inden du køber certifikaterne, burde du tjekke
		udstederens renter.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Ved Nordnet (via Nordea) er renterne hovedsageligt baseret på 3 kriterier. Hvis Gearing er under
		X5, betaler man 1,0% rente. X5 gearing koster 1,5% rente og derover koster 3% rente. De
		informerer dog, at renten kan varierer ud fra det underliggende værdipapir.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Hvordan indberegnes renterne?</h3>
	<!-- /wp:heading -->

	<!-- wp:quote {"className":"is-style-large"} -->
	<blockquote class="wp-block-quote is-style-large">
		<p>
			Gebyret opkræves ved at reducere produktets værdi med beregningsformlen: <strong
				>(Gearing - 1) * Renter i % / 365</strong
			>.
		</p>
		<cite
			><a class="rank-math-link" href="https://www.nordnet.dk/dk/marked/nordnet-markets/gebyrer"
				>https://www.nordnet.dk/dk/marked/nordnet-markets/gebyrer</a
			></cite
		>
	</blockquote>
	<!-- /wp:quote -->

	<!-- wp:paragraph -->
	<p>
		<em
			><span class="has-inline-color has-luminous-vivid-amber-color"
				>Jeg vil først og fremmest gøre dig opmærksom på at Nordnet omregner årlig rente til daglige
				renter forkert, så du hvis du holder et certifikat i mere end 1 år, så betaler du mere end
				den anviste rente på grund af den daglige renters rente effekt.</span
			></em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du køber et certifikat med X10 gearing, så kommer du til at betale 3% i rente. Hvis du
		bruger Nordnet's egen beregning, vil din daglige rente være (10-1)*0,03/365= 0,000739 (0,0739%).
		Denne rente bliver påregnet hver dag når markedet lukker.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<em
			><span class="has-inline-color has-luminous-vivid-amber-color"
				>Vil bare lige vise dig at beregningen giver mere, ved at sige (1,000739^365-1)/9= 0.03438
				(3,438% årligt), bare så du er opmærksom på dette!</span
			></em
		>
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Lad os sige at du du køber 10 X10 BULL Certifikater i Apple til 1.800 kroner i stykket, altså en
		samlet investering på 18.000 kroner. Da din gearing er X10, svare din investering til 180.000
		kroner, hvor 18.000 af dem er dine egne penge.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li>Investering: 180.000 kroner</li>
		<li>Gearing: 162.000 kroner</li>
		<li>Indskud: 18.000 kroner</li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Dagen hvor du købte certifikatet er der ingen renter, da de første bliver påregnet når markedet
		lukker. Din investering kan derfor tåle at falde <em>18.000/180.000*100=</em> 10% (ned til 1.620
		kroner per aktie) i værdi, før certifikatet udløber. Når dagen er omme pålægger vi renterne af det
		lånte beløb. Det vil sige 162.000*0,000739= 119,718 kroner, som vi pålægger gearingen og trækker
		fra vores indskud.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li>Investering: 180.000 kroner</li>
		<li>Gearing: 162.119,72 kroner</li>
		<li>Indskud: 17.880,28 kroner</li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Næste dag er vores certifikater mere skrøbelig overfor nedgang, da vi stadig skal betale lånet
		tilbage ved udløb. Efter gårsdagens renter, kan vi nu tåle at kursen falde <em
			>17.880,28/180.000*100=</em
		> 9,933% (ned til 1621,20 kroner per aktie) i værdi, før certifikatet udløber.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det vil sige at aktien skal modsvarende stige <em>18.000/17.880,28*10=</em> 10,066% for at udligne
		renten. Herefter lave du samme beregning ud fra de nye tal, indtil indskuddet rammer eller falder
		til under 0 kroner og udløber.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Hvordan tages der højde for udbytter?</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du ikke ejer det underliggende værdipapir, så har du ikke retten til at modtage udbytter.
		Her har man valgt at modregne udbytter på certifikatet, så du ikke kan udnytte kursfaldet fra
		udbytter til at købe BEAR certifikater. Hvis dit værdipapir på 100 kroner udbetaler 5 kroners
		udbytte, så modregner man de 5 kroner på certifikatet, så det stadig er 100 kroner værd.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvad er risikoen ved BULL og BEAR Certifikater?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du køber BULL og BEAR Certifikater, så er din endelige risiko det investerede kapital. Hvis
		du har investeret 100 kroner, så vil du kun tabe de 100 kroner. Du skal dog være opmærksom på,
		at jo højere Gearing du bruger, des hurtigere kan du tabe investeringen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har X10 gearing, så mister du din investering hvis det falder 10%, da du også skal
		betale de lånte 90%. Med andre ord, hvis du har investeret 100 kroner med X10 gearing, så har du
		en investering på 1.000 kroner. Herudaf er 900 kroner lånt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis investeringen falder 10%, så falder din samlede investering til 900 kroner, svarende til
		det gearede/lånte beløb, og investeringen er derfor tabt. Hvis du holder på den gearede
		investering i flere dage, så kommer der renter, og du kan derfor tåle mindre tab.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvordan beskattes BULL og BEAR Certifikater?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når du investerer igennem disse CLC eller CFD kontrakter (ref. certifikater), så bliver de
		beskattet efter kursgevinstloven, som er opgjort efter kildeskatteloven. Det vil sige at disse
		certifikater er beskattet sammen med din normale indkomstbeskatning. Du burde derfor være
		opmærksom på om du overkommer indkomstgrænsen for topskat.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:quote -->
	<blockquote class="wp-block-quote">
		<p>
			<strong>§ 12.</strong>&nbsp;Personer, der er skattepligtige efter kildeskatteloven, og
			dødsboer, der er skattepligtige efter dødsboskatteloven, skal ved opgørelsen af den
			skattepligtige indkomst medregne gevinst og tab på fordringer og gæld, som omfattes af § 1,
			efter de regler, der er angivet i dette kapitel og § 29 samt i kapitel 4, 5 og 7.
		</p>
