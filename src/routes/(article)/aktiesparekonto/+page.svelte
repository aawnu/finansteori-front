<h1>Aktiesparekontoen - Kun 17% skat af dine investeringer</h1>

<div class="wp-article">
	<p>
		Start en aktiesparekonto og betal en fordelagtig lav beskatning på kun 17%. I 2020 kan du
		indskyde helt op til 100.000 kroner på din aktiesparekonto, og investere dem i både aktier,
		fonde og ETF'er. Slip for at betale helt op til 42% af dine afkast!
	</p>

	<h2>Hvad er en aktiesparekonto?</h2>

	<p>
		Sverige har et system de kalder Investeringssparkonto ("ISK"), hvor man betaler en lavere skat
		af sine investeringer, så pengene forbliver i markedet. Det princip har vi lånt i Danmark og
		udgivet med vores egen regelbog.
	</p>

	<p>
		I 2018 fik vi nemlig Aktiesparekontoen ("ASK"). Det blev muligt at investere din opsparing i
		markedet, til en fordelagtig beskatning. Det var planen at kontotypen skulle starte med et lavt
		loft, og herefter løftes årligt. Desværre kan politikerne ikke blive enige om hvor meget eller
		hvornår, loftet skal hæves.
	</p>

	<h2>Hvem kan åbne en aktiesparekonto?</h2>

	<p>
		Der er nogle specielle regler for den nye type konto. Du skal være fuldt eller begrænset
		skattepligtig i Danmark, for at kan åbne en aktiesparekonto. Dertil er der regler for hvor du
		kan åbne den nye kontoform. Hør din bank eller aktiemægler, om de kan tilbyde dig en
		aktiesparekonto.
	</p>

	<p>
		Formålet er at du skal efterlade flere penge i markedet, frem for at have dem til at stå stille
		på en opsparingskonto. Sådan kan din opsparing nemlig bidrage til den økonomiske vækst og
		stabilitet.
	</p>

	<p>
		De fleste vælger at oprette en <a
			href="https://www.home.saxo/da-dk/investor/aktiesparekonto"
			class="rank-math-link">aktiesparekonto ved Saxo Bank</a
		>, men du kan åbne dem ved de fleste banker, som tilbyder investeringkontoer.
	</p>

	<h2>Hvad kan jeg investere i, med en aktiesparekonto?</h2>

	<p>
		Aktiesparekontoen har nogle begrænsninger, som du ikke finder på en normal investeringskonto.
		Hvis du selv vil læse op på dette område, kan du åbne <strong>C.B.5 Aktiesparekontoloven</strong
		> nede i kilderne.
	</p>

	<!-- wp:list -->
	<ul>
		<li>
			Du må oprette 1 aktiesparekonto, hvorpå du maksimalt må indskyde op til loft beløbet. I 2020
			er loftet på 100.000,- kroner.
		</li>
		<li>
			Du må investere i børsnoterede aktier og fonder, der er beskattet som aktieindkomst (ref.
			efter aktieavancebeskatningen). Her under udloddende fonde og ETF'er.<br /><em
				>Note: Du har kun adgang til investeringsmål, som er tilladt efter lovgivningen, når du
				investere via din ASK. Hvis du er i tvivl så kontakt din aktiemægler.</em
			>
		</li>
		<li>Du må ikke investere din pension eller igennem virksomhed.</li>
	</ul>
	<!-- /wp:list -->

	<p>
		Hvis du vil have mere frihed til at investere, kan du altid åbne en normal investeringskonto.
		Her er en <a
			class="rank-math-link"
			href="https://finansteori.dk/borshandel/alt-du-burde-vide-om-aktieinvestering-komplet-introduktion/"
			>komplet introduktion til aktieinvestering</a
		>.
	</p>

	<h2>Hvor meget må jeg indskyde på en aktiesparekonto?</h2>

	<p>
		Skat har lavet nogle gode eksempler under <strong>C.B.5 Aktiesparekontoloven</strong>. De
		beskriver hvornår, hvordan, og hvor meget du må indskyde på kontoen. Hvert år opgøre politikerne
		et loft for, som afgøre hvor meget du må indskyde på kontoen.
	</p>

	<p>
		I 2020 blev loftet sat op til 100.000 kroners værdi. Du må indskyde en værdi op til 100.000
		kroner, uanset om det er frie midler (penge), aktier, eller investeringsbeviser. Det eneste krav
		er at midlet må ejes på denne kontotype.
	</p>

	<p>
		Hvis du tidligere har indskudt penge på kontoen, eller har haft et årsopgørelse, så skal det
		medregnes imod dit indskudsloft. For at give et eksempel, hvis du indskød og investerede 10.000
		kroner i 2019. Hvis investeringen er faldet til 7.000 kroner ved årsopgørelsen, så må du i 2020
		indskyde 100.000-7.000= 93.000 kroner. Var værdien af din konto opgjort til 16.000 kroner, må du
		kun indskyde 100.000-16.000= 84.000 kroner i 2020.
	</p>

	<h2>Hvad er det nye skattegrundlag?</h2>

	<p>
		Hvis du åbner en normal investeringskonto, så bliver du beskattet af aktieindkomst efter en
		progressionsgrænse. I 2020 er grænsen 27/42, med en overgang på 55.300 kroner. Det vil sige at
		du betaler 27% skat de første 55.300 kroner, du tjener fra aktieindkomst, og 42% af resten. Det
		kan både beskattes efter realisationsprincippet, når du sælger din investering, eller
		lagerprincippet.
	</p>

	<p>
		Aktiesparekontoen har en særskat på kun 17%, og er beskattet efter lagerprincippet. Det vil sige
		at du hvert år betaler skat af dit positive afkast, uanset om du har solgt din investering.
		Hertil er aktiesparekontoen isoleret fra alle andre aktieindtægter og tab.
	</p>

	<p>
		Dertil er aktiesparekontoen isoleret på dit skattekort, så dine afkast bliver ikke opgjort samme
		med normale investeringskontoer. Det vil sige at negative afkast på aktiesparekontoen ikke kan
		trækkes fra normale aktieindtægter eller omvendt.
	</p>

	<p>
		<em
			>Hvis du er begrænset skattepligtig i Danmark, betaler du 15% i stedet for 17%. Den her
			artikel tager udgangspunkt i fuld skattepligt, så derfor er alle eksempler oplyst med 17%.</em
		>
	</p>

	<h2>Hvordan fungere beskatningen?</h2>

	<p>
		Når du investere igennem en aktiesparekonto, er din bank eller udbyder ansvarlig for at oplyse
		dine afkaster og udbytter til skat. Her bliver du klogere på, hvordan det bliver opgjort. Så ved
		du hvordan du kan følge med, og cirka-beregne, hvor meget du skal betale i skat.
	</p>

	<p>
		Ifølge lagerprincippet har man et beskatnings-år fra 1. Januar til 31. December. Alle
		investeringer bliver opgjort den 31 December, og så får de en ny start-kurs, til næste års
		beskatnings-opgør.
	</p>

	<p>
		Du bliver altså ikke beskattet i løbet af året, når du modtager udbytter, køber, eller sælger
		dine investeringer. Det bliver du derimod i ved opgørelsen den 31. December. Hvis du køber og
		sælger dine aktier i løbet af året, bliver gevinsten eller tabet også opgjort og beskattet her.
	</p>

	<p>
		Når skatten er opgjort, skal udbyderen af kontoen trække beløbet, uanset om der er penge nok på
		kontoen. Det er derfor vigtigt at være forberedt på eventuelle beskatninger, så du ikke ender
		med et overtræk.
	</p>

	<h2>Tilladt at dække sine skatter</h2>

	<p>
		Hvis du i 2019 har haft et positivt afkast på 3.000,- kroner og fået udloddet 150,- kroner i
		udbytte. Så skal du betale 17% skat af 3.000+150= 3.150,- kroner. Det vil sige 535,50 kroner,
		som din udbyder trækker fra din konto i 2020.
	</p>

	<!-- wp:quote -->
	<blockquote class="wp-block-quote">
		<p>
			Der kan dog uanset beløbsgrænsen foretages indskud på aktiesparekontoen til betaling af
			skat.&nbsp;Det gælder for både fuldt og begrænset skattepligtige. Indskuddet skal foretages i
			det kalenderår, hvor skatten forfalder til betaling. Se ASKL (ref. Aktiesparekontoloven) § 9,
			stk. 2, og § 20.
		</p>
		<cite>C.B.5 Aktiesparekontoloven</cite>
	</blockquote>
	<!-- /wp:quote -->

	<p>
		Det vil sige at beskatningen er sat til opkrævning i 2020, og du må derfor overføre 535,50
		kroner til at dække skatten, uden at det betragtes som indskud i henhold til loftet.
	</p>

	<h2>Udbytteinvestering med en aktiesparekonto</h2>

	<p>
		Hvis du er interesseret i at investere i udbytteaktier, så ville platformen normalt trække
		skatten fra ved modtagelsen. På en aktiesparekonto skal du ikke betale skat før kontoens årlige
		opgørelse. Det betyder at du får det fulde udbytte udbetalt, som du kan geninvestere i markedet.
	</p>

	<p>
		Du kan vælge at tilsidesætte 17% af dine udbytter, men du opnår større rentes rente effekt, hvis
		du geninvestere det fulde beløb. Renters rente ved investering går ud på at geninvestere sine
		udbytter i flere udbytteaktier. På den måde opbygger man langsomt en forøget udbytteindtægt.
	</p>

	<h2>Invester billigere i ETF'er</h2>

	<p>
		ETF'er er en betegnelse for børsnoterede investeringsselskaber. Her bliver du beskattet efter
		lagerprincippet, hvor du årligt betaler skat af afkast og udbytte. Istedet for at betale 27/42%
		efter de normale aktieskatteregler, så kan du altså nøjes med at betale 17%. Det giver dig en
		stor skatte fordel, når du alligevel beskattes årligt.
	</p>

	<h2>Opsummering: Hvorfor burde jeg investere igennem aktiesparekontoen?</h2>

	<p>
		Aktiesparekontoen er en fantastisk mulighed, for dig som gerne vil investere i udbytter, ETF'er
		og andre lagerbeskattede investeringer. Istedet for at betale en høj skat på afkast og udbytter,
		kan du drage fordel af den lave beskatning aktiesparekontoen tilbyder.
	</p>

	<p>
		Hvis du har en månedlig indtægt, som kan dække beskatningen på 17%, så får du fordelen af at kan
		geninvestere 100% af dine udbytter. Det forøget dine rentes rente helt op til 58%, da du slipper
		for at betale op til 42% skat bed udbetalingen af udbytten.
	</p>

	<p>
		Vil du investere i indeks ETF'er eller udbytte-foreninger, som alligevel er lagerbeskattet
		årligt. Så kan du spare helt op til 40% i skat, hvis din investeringsportefølje overgår 55.300
		kroner - altså progressionsgrænsen for aktiebeskatning.
	</p>

