<h1>Aktiesplit - Udvandet ejerskab og lavere priser, er det en god ting?</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Hvis et selskab bliver for dyrt for den generelle investor, så kan de vælge at udføre et
		aktiesplit. Det vil sige at hver aktie bliver lavet om til flere aktier, så kursen tilsvarende
		falder. Hvorfor giver det mening at udføre et aktiesplit? I skrivende stund har vi 2 eksempler
		på aktiesplit, fra både Apple med 4:1 og Tesla med 5:1.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:heading -->
	<h2>Hvad er et aktiesplit?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Et børsnoteret selskab består af aktier, som vi køber for at opnå ejerskab i selskabet. Kursen
		af aktierne afgøres af udbuddet og efterspørgslen, hvad folk vil købe og sælge deres aktier for.
		Interessen for et selskab kan blive så stor, at den generelle investor ikke har råd til at købe
		dem.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Her kan selskabet vælge at udføre et aktiesplit. Det betyder at hver aktie bliver omstruktureret
		til flere aktier. For eksempel er <a
			aria-label=" (opens in a new tab)"
			rel="noreferrer noopener"
			href="https://investor.apple.com/faq/default.aspx"
			target="_blank"
			class="rank-math-link">Apple igang med at udføre et 4:1 aktiesplit</a
		>, hvilket betyder at hvis du ejer 1 aktie nu, så får du tildelt yderligere 3 aktier. Du vil
		altså ende med at have 4 aktier, for hver aktie du allerede ejer allerede.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Record Date</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Det er en smule indviklet, men "record date" er dagen hvor selskabet opgøre hvem der har retten
		til at modtage de nye aktier og udsteder dem. Det er dog ikke nu de nye aktier træder i kræft,
		så hvis du sælger aktien efter record date, så sælger du også retten til at modtage de nye
		udstedte aktier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Man kan godt sige at det er her aktierne øremærkes til at modtage de nye aktier, så virksomheden
		(f.eks Tesla) kan holde øje med, hvem der har retten til at modtage de nye aktier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Split Date</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Når markedet lukker på "Split Date", så opgøres det hvem der har rettighederne til at modtage de
		nye udstede aktier. Det er altså her de gøres effektive og træder i kraft. Det er altså her
		selskabet (f.eks Apple) sender de nye aktier ud til dem som ejer aktien og har retten til de nye
		udstede aktier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det er derfor vigtigt at købe aktierne før markedet lukker på Split Date, hvis du vil tage del i
		det aktuelle split.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading {"level":3} -->
	<h3>Ex Date</h3>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Ligesom med udbytter så markere "Ex Date" (eller X-dagen) hvornår den nye kurs træder i kræft.
		Ved udbytter er det her kursen falder tilsvarende udbyttet, men ved aktiesplit er det her
		aktiens kurs fordeles ud på de nye aktier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvorfor deler man aktierne op i flere?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Apple har i skrivende stund en aktiekurs på godt 460 USD (~2.893 DKK) og Tesla er på godt 1.650
		USD (~10.376 DKK). Det er de færreste som har råd til at købe mere end et par Apple aktier,
		eller bare en enkelt Tesla aktie. Det er specielt dyrt, hvis man gerne vil opnå Dollar Cost
		Average effekten, hvor man opkøber flere aktier løbende. Lad os tage et kig på aktiesplittet:
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list -->
	<ul>
		<li><strong>Apple 4:1</strong> &gt; 460/4= 115 USD &gt; ~723 DKK per aktie</li>
		<li><strong>Tesla 5:1</strong> &gt; 1.650/5= 330 USD &gt; ~2.075 DKK per aktie</li>
	</ul>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Der er flere som har råd til aktierne efter aktiesplittet, og derved kan efterspørgslen stige
		markant. Det kan medføre at aktierne handles hurtigere, så det både er nemmere at købe og sælge
		aktierne. Der er oftest en relation imellem hvor nemt det er at omsætte en aktie, og hvor
		risikovillige folk er omkring at investere.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis volumen bliver stor nok, så kan det også invitere Day Traders, som <a
			href="https://finansteori.dk/borshandel/day-trader-invester-daglige-markedet"
			class="rank-math-link">handler store mængder aktier for korte tidsperioder</a
		>. Det kan frembringe endnu større handels volume, og måske gøre aktien mere volatil (have
		større kursændringer).
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvornår skal man opkøbe eller sælge?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Der er meget spekulation omkring aktiesplit, om hvornår man skal købe eller sælge aktien. Hvis
		du tror at efterspørgslen bliver så høj, at aktien stiger i pris, så kan det være en fordel at
		købe aktien før splittet. Tror du omvendt at aktien vil falde i pris, fordi folk sælger en del
		af deres beholdning, så giver det mening at vente.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		<a
			href="https://finansteori.dk/borshandel/udbytteinvestering-aktier-fonde-etf"
			class="rank-math-link">Hvis du investere imod udbytter</a
		>, så skal du være opmærksom på 2 ting.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:list {"ordered":true} -->
	<ol>
		<li>
			Udbytteprocenten kommer til at stige, fordi den er historisk visende. Du kommer ikke til at få
			større udbytte, fordi den skal fordeles ud på alle aktier.
		</li>
		<li>
			Udbyttet per aktie kommer til at falde tilsvarende, så hvis du ejer 1 aktie før, opnår du
			samme udbytte som 4 aktier efter.
		</li>
	</ol>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Kort sagt, hvis 1 Apple aktie udbetaler 5 DKK per aktie før splittet, så vil du efterfølgende
		modtage 1,25 DKK per aktie, hvis selskabet udbetaler samme udbytte. Selskabet annoncere ikke
		udbytte imod aktien, men som en procentdel af deres overskud, som efterfølgende fordeles ud på
		alle aktierne.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har tænkt dig at købe et antal aktier, som går op i fordelingen, så giver det ikke meget
		mening at vente til efterfølgende. Du får ikke mere ud af at købe 4 aktier til 250 kroner, end 1
		aktie til 1.000 kroner. Hvis du derimod gerne vil købe 2.5 aktier før splittet, altså 6 aktier
		efter splittet. Så kan det give mening at vente, så du ikke skal betale kurtagen over 2 aktier.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Hvorfor bruger man ikke bare fraktionelle aktier?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Nogle platforme giver dig muligheden for at købe en fraktionelle aktier, altså brøkdele af en
		aktie. Hvis du ikke har råd til at betale 1.000 kroner for en aktie, så giver platformen dig
		muligheden for at købe 10% for 100 kroner istedet for. Det vil sige at platformen køber aktien
		til 1.000 kroner og derefter giver dig et syntetisk værdipapir, som svare til 10% af en aktie.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis aktien stiger til 1.500 kroner, så stiger dit syntetiske papir til 150 kroner. Hvis du nu
		køber for yderligere 100 kroner, så køber du kun 6.667%. Dit syntetiske ejerskab udgøre altså
		16.667% af en aktie nu. Når du opnår 100%, så omdannes dit syntetiske produkt til en aktuel
		aktie.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du nu ejer 25% af en Apple aktie før splittet, så vil du efterfølgende eje 25% af 4 aktier,
		eller 100% af 1 aktie. Det giver platformen muligheden for at omdanne din syntetiske aktie til
		en aktuel aktie og derefter frasælge resterende 3 aktier. På den måde kan platformen minimere
		sin risiko imod deres del af en aktie. Det har altså ikke nogen større effekt for dig, men kan
		gøre en kæmpe forskel for investerings platformene.
	</p>
