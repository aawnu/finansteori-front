	<h2>Hvor er risikoen ved udbytteinvesteringer?</h2>
	<!-- /wp:heading -->
	
	<!-- wp:paragraph -->
	<p>Når du investerer i udbytte, er der samme risikoer som ved normale aktier. Kursen kan falde ved manglende interesse. Virksomheden kan blive ramt af udfordringer eller konkurs. Der kan komme en situation hvor virksomheden ikke må udbetale eller ikke kan finde overskud nok til at udbetale udbytter.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Du kan også risikerer at virksomheden har en lavere omsætning et år. Det kan medføre at udbytter bliver mindre eller fjernet. Hvis virksomhed vurderer at pengene skal gemmes til næste regnskabsår, for at sikre dens selv finansiering.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Her er det vigtigt at huske at udbytter og kursværdi ikke går i hånd. Kursen kan være faldende og udbyttet stigende. Det er dog sjældent at virksomheden vækster og nedsætter sine udbytter.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Dertil ejer du stadig aktien hvis virksomheden nedsætter eller fjerner sin udbytte. Så hvis tabet er minimalt på salget, så har du stadig en generel gevinst fra at have ejet aktien og modtaget udbytter.</p>
