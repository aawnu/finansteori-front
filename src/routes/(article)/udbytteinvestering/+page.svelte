	<h2>Hvad er udbytte?</h2>
	<!-- /wp:heading -->
	
	<!-- wp:paragraph -->
	<p>Når en virksomhed er blevet moden nok til at kan finansiere sig selv. Skaber et stabilt årligt overskud, efter at have betale alle regninger og lagt penge til siden til fremtiden. Så kan bestyrelsen vælge at belønne virksomhedens investorer. Det gør de ved at udbetale en del af årets overskud i løbet af næste år.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Her skal du være opmærksom på, at en virksomhed skal betale selskabsskat før det kan udbetale udbytter. Derfor er det typisk opgjort ved årsresultatet, når virksomheden opgøre overskuddet fra omsætningen og hvor meget det skal betale i selskabsskat.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Når virksomheden udbetaler en del af sit overskud, så falder aktiekursen tilsvarende udbyttebeløbet. Det vil sige at hvis en aktie koster 80 kroner og udbetaler 6 kroners udbytte, så vil aktiekursen falde til 74 kroner ved udbetalingen. Herefter vil den stige og falde som normalt, ud fra hvad folk værdisætter aktien.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:heading -->
	<h2>Hvorfor betales der udbytte?</h2>
	<!-- /wp:heading -->
	
	<!-- wp:paragraph -->
	<p>Hvis en virksomhed har 100.000.000 kroner i overskud efter alle regninger og skat, så kan de vælge at udbetale en del til dets aktionærer. De vil gerne have penge nok i banken til fremtidige gøremål, så derfor udbetaler de kun 10% af overskuddet.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Lad os sige at virksomheden har 1.275.947 aktier. Virksomheden betaler 10% af 100.000.000, svarende til 10.000.000, fordel ud på dets aktier. Hver aktie er derfor berettiget til at modtage <em>10.000.000/1.275.947=</em> 7,83731 kroner. I det fleste tilfælde afrunder man udbytte ved 3-5 decimaler.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Udbyttet er altså ikke baseret på aktiekursen. Man kan dog se hvor meget der historisk er blevet udbetalt i forhold til aktiens nuværende kurs. Hvis aktien koster 82 kroner, så vil udbytteprocenten sige <em>7,83731/82*100=</em> 9.56%. Stiger aktien imorgen til 102 kroner, så vil udbytteprocenten tilsvarende falde til <em>7,83731/102*100=</em> 7.68%.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Når bestyrelsen vælger at udbetale udbytte, vil der kommer 2 vigtige datoer. Der kommer en X-dato og en Udbetalings-dato. X-datoen er den dag, hvor virksomheden opgøre hvem der har ret til udbyttet.</p>
