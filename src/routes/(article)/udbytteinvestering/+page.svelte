	<h2>Hvad er renters rente effekten?</h2>
	<!-- /wp:heading -->
	
	<!-- wp:paragraph -->
	<p>Hvis du køber 1000 aktier til 80 kroner, af en virksomhed som betaler fast månedlig udbytte, lad os sige 1.35 kroner. For at gøre eksemplet nemt, siger vi at kursen stiger 2% om måneden og vi indberegner <strong>ikke </strong>skat.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>I første måned modtager du 1.350 kroner, og aktien falder til 76,50 kroner per aktie. Du har råd til at købe 17 aktier mere i selskabet. I løbet af måneden stiger aktien til 78,03 kroner. Der udbetales nu udbytte og den falder til 76.68 kroner. Da du ejer 1.017 aktier, modtager du nu 1.372.95 kroner, som du geninvesterer i 18 aktier mere.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Nu har du samlet 1.035 aktier, som betaler 1,35 kroner om måneden. På den måde kan du opnår du renters rente effekten ved at geninvesterer udbyttet imod flere udbytter i fremtiden. Hvis du dertil selv overføre penge månedligt, så forøger du dit afkast endnu mere fra måned til måned.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Det svarer i bund og grund til at dine aktie stiger x% hver måned, men udbytte giver muligheden for at sprede afkastet ud i flere forskellige aktier. Du kan også vælge at købe varer for pengene, uden at skal sælge dine investeringer.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:heading -->
	<h2>Husk regnestykket Y=72/R</h2>
	<!-- /wp:heading -->
	
	<!-- wp:paragraph -->
	<p>Når du investerer og geninvesterer udbytter, så er start-målet som regel at indfri sin investering. Det vil sige at man beregner hvor lang tid der går, får man har fået sine penge ud af investeringen, uden at sælge den.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Der er nogle matematikere, som har beregnet på eksponentielle stigninger. Her er man kommet frem til 2 tal, som bruges på hver sin måde, i forhold til hvor præcist man vil være.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Hvis man vil have en cirkaberegning på hvor lang tid renter og renters rente vil tage om at fordoble, så kan man bruge 72-reglen. Det gør man ved at lave regnestykket Y=72/R, hvor Y er resultatet af år og R er renten. Hvis dit årlige afkast er 7%, så vil det fordoble efter 72/7= ~10 år, da man altid afrunder kommatallene på 72-reglen.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Hvis vi vil dobbelt tjekke hvor precise vores tal er, så kan vi sige 1+R/100^Y. I vores tilfælde vil det sige 1,07^10= 1.97, hvilket er ret tæt på en fordobling.</p>
