	<p>Først skal vi finde ud af hvor meget vi skal tjene om året, for at have 18.000 kroner om måneden. 18.000*12 er 216.000 kroner efter skat. Her bliver det lidt tricky, fordi vi er progressions-beskattet 27/42%. For at gøre det nemt for os selv, så vælger vi at modregne den højeste skattesats på 42%.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Det vil sige at vi skal modtage 216.000/(1-0,42)= 372.413,80 kroner før skat. Her er det vigtigt at man altid runder up til 2 decimaler. Du skal også huske at aktier er aktieavancebeskattet og ikke som indkomst. Du vil derfor ikke komme op på 52% top-skat. Så hvor meget skal vi investerer for at opnå 372.413,80 kroners afkast om året?</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Her er det vigtigt at forholde os til 2 variabler. Gennemsnitlig inflation og gennemsnitligt udbetalt afkast (ref. udbytter). Her er man nået frem til at man gennemsnitligt afkaster 7% om året. Her må man trække 4% ud og de sidste 3% skal geninvesteres imod inflation.</p>
	<!-- /wp:paragraph -->
	
	<!-- wp:paragraph -->
	<p>Det betyder at 4% af vores portefølje skal svare til 372.413,80 kroner. For at modregne det, siger vi 372.413,80/0,04= 9.310.345 kroner. Du skal altså akkumulerer en formue på lige knap 9,5 millioner kroner, for at kan dække 18.000 kroner om måneden.</p>
