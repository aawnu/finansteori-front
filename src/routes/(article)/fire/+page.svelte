<h1>FIRE: Økonomisk uafhængig som tidligt pensioneret</h1>

<div class="wp-article">
	<!-- wp:paragraph -->
	<p>
		Har du en drøm om at blive økonomisk uafhængig i en ung alder, så du kan pensionere dig fra
		arbejdsmarkedet? Så er du sikkert interesseret i at lærer mere om FIRE fællesskabet. Her er
		målet oftest at holde udgifterne lave, så man kan leve af sine investeringer hurtigere.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:more -->
	<!--more-->
	<!-- /wp:more -->

	<!-- wp:paragraph -->
	<p>
		Trods udtrykket FIRE står for 'Financial Independence and Retired Early' (oversat til "Økonomisk
		uafhængig som tidligt pensioneret"), så er der mange som forbliver på arbejdsmarkedet deltid.
		Målet er altså at kan betale sine regninger og levestil, uanset om man arbejder eller ej.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Trinity Study: 4-procents reglen</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		I Amerika lavede man i 1998 et studie, hvor man analyserede børsmarkedet imellem 1925 og 1995.
		Målet med studiet var at finde ud af, hvordan man bedst muligt kan pensionere ved at trække
		penge ud af sin portefølje.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>Da studiet blev udført, fandt man frem til 3 interessante nøgletal:</p>
	<!-- /wp:paragraph -->

	<!-- wp:list {"ordered":true} -->
	<ol>
		<li>Det amerikanske markedet stiger gennemsnitligt 7% om året.</li>
		<li>Den amerikanske inflation udgøre gennemsnitligt 3% om året.</li>
		<li>Hvis man lader inflationen forøge sig selv i markedet, kan man trække 4% ud om året.</li>
	</ol>
	<!-- /wp:list -->

	<!-- wp:paragraph -->
	<p>
		Studiet er lavet over forskellige portefølje-opsætninger, lige fra 100% aktier og gradvist over
		til 100% obligationer. Her er det vigtigt at vide, at studiet er lavet ud fra, at man pensionere
		sig selv om 60 år gammel, og skal leve af det i 20-25 år.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det vil sige at man skal modregne de 4%, for at finde ud af hvor meget du skal have investeret.
		Efter som studiet er lavet i USA, hvor man stort set ikke betaler skat af pension, så bør du
		medregne den forventede skat i danmark.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du har brug for 16.000 kroner om måneden efter skat, så skal du regne det om til årligt
		rådighedsbeløb. Det vil sige at du skal bruge 192.000 kroner om året efter skat. I danmark har
		vi en 27/42% progressionsskat af aktieindkomst, hvor grænsen er ved 55.300 kroner i 2020.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Jeg vil derfor anbefale at du modregner de 42%, så du ikke skal rode med progressionsgrænsen. <em
			>192.000/(1-0,42)=</em
		>
		331.034,48 kroner om året før aktieskat. Hvis vi så forholder os til de normale 4% om året, så
		kan du gange tallet med 25. <em>331.034,48*25=</em> 8.275.862 kroner, som du skal have investeret
		for ca 25 års pension af 4% udtræk om året.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis din pension skal være længere end 25 år, så skal du modregne en mindre procentdel, og
		derved have investeret et større beløb. Du kan finde beregningen af procent ved at sige
		"X=100/pensionsår" og derefter modregne årlige budget før skat med X i matematisk procent. 30 år
		vil f.eks være <em>100/30=</em> 3,34% og <em>331.034,48/0,0334=</em> 9.911.211,97 kroners opsparing,
		hvor du kan trække 3,34% ud årligt.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>FIRE på udbytte investeringer</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Jeg har skrevet en artikel omkring <a
			href="https://finansteori.dk/borshandel/udbytte-investering-sadan-kommer-du-igang"
			class="rank-math-link">at investerer i udbytter</a
		>, altså at købe dele af virksomheder, som udbetaler en del af årets resultat til ejerne. Det er
		også en god måde at kan opnå delvis eller fuld finansiering af dine udgifter.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Fordelen ved udbytte investering er at du ikke skal sælge ud af din portefølje, men det kræver
		lidt mere disciplin at holde styr på indtægter og udgifter. Ikke alle virksomheder betaler
		udbytter på samme sid, dertil er kan de udbetale med forskellige intervaller.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det vil sige at 3 virksomheder kan betale udbytte i April, 2 i Juni og 5 i August. Her er din
		opgave at holde styr på disse indtægter og fordele dem ud på 12 måneder. Oftest køre man dem
		bagud, så det du tjener i 2019, bruger man i 2020.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Du kan investerer i udbytte-selskaber som følger samme kalender, hvor man får udbytter
		månedligt. Det kan du for eksempel bruge til at modregne månedlig rådighedsbudget og aktieskat,
		så du ikke skal håndtere et helt år af gangen.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis du stadig skal bruge 16.000 om måneden efter skat, skal du have <em>16.000/(1-0,42)=</em> 27.586,21
		kroner før skat. Her skal du kigge på hvor meget en aktie udbetaler, hvor ofte, og hvad den koster.
		Hvis du skal have ~28.000 kroner fra Danske bank, som gennemsnitligt udbetaler 7 kroner, per aktie,
		om året. Skal du altså eje 4.000 af deres aktier, og så har du kun dækket 1 måned ud af 12.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Hvis vi antager at en aktie gennemsnitligt udbetaler 4% om året, kan vi altid modregne hvor
		meget vores portefølje vil koste os. Udbytte-procenten er altid baseret på nuværende aktiepris.
		Det vil altså koste <em>28.000/0,04=</em> 700.000 kroner at modtage 28.000 kroner. Hvis du skal
		have alle 12 måneder, skal du altså investerer <em>331.034,48/0,04=</em> 8.275.862. I det her tilfælde
		ender det samme sted som Trinity-studiet, bare hvor porteføljen udbetale 4% i udbytte frem for at
		du sælger 4%.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:heading -->
	<h2>Opsparing via selskab</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		Hvis du er interesseret i at akkumulere din formue, hvor du kommer til at modtage udbytter, så
		kan det være en fordel at stifte selskab. Lige nu kræver det 40.000 kroner + advokat gebyr at
		stifte et ApS (i 2020). De 40.000 mister du ikke, da de bliver overført som kapital til
		selskabet. Advokat gebyret går dog tabt, da advokaten også skal tjene penge.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Fordelen ved et selskab er den lavere beskatning. Den eneste måde du kan få lavere beskatning
		under akkumulationen, er via aktiesparekontoen, som lige nu (i 2020) har et loft på 100.000
		kroner.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:table {"hasFixedLayout":true,"className":"is-style-stripes"} -->
	<figure class="wp-block-table is-style-stripes">
		<table class="has-fixed-layout">
			<tbody
