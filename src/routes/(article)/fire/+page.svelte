					></tr
				></tbody
			>
		</table>
	</figure>
	<!-- /wp:table -->

	<!-- wp:paragraph -->
	<p>
		Selskaber er som oftest lagerbeskattet, hvilket betyder at man betaler skat hvert år, uanset om
		du realisere gevinsten eller ej. Det vil sige, at hvis du i Januar køber en aktie til 100
		kroner, som i slut december er 200 kroner værd, skal selskabet beskattes af de 100 kroners
		kursstigning, uden at du sælger aktien. På samme måde er aktiesparekontoen også lagerbeskattet.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Det vil sige at det er vigtigt at du finder penge hvert år til at betale skatten, enten ved at
		modtage udbytter, sælge eller skabe en indtægt på anden vis. På aktiesparekontoen kan du dække
		med private penge, fra løn eller andet. I selskabet kan du også indskyde kapital, eller låne
		penge til selskabet. Du skal bare være sikker på at gøre det rigtigt, så du ikke ender i
		problemer med skat.
	</p>
	<!-- /wp:paragraph -->

