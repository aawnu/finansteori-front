	<!-- wp:heading -->
	<h2>Opsummering: Hvad er FIRE?</h2>
	<!-- /wp:heading -->

	<!-- wp:paragraph -->
	<p>
		FIRE er et fællesskab der har til formål at pensionere sig selv fra arbejdsmarkedet, eller som
		udgangspunkt ikke være afhængig af at skulle arbejde. Mange forsøger at leve så billigt som
		muligt, så deres levevilkår hurtigt kan dækkes af deres investeringer. På den måde har de et
		sikkerhedsnet at falde tilbage på, hvis de mister sit arbejde.
	</p>
	<!-- /wp:paragraph -->

	<!-- wp:paragraph -->
	<p>
		Er FIRE noget for dig? Det skader aldrig at have en ekstra indkomstkilde, som ikke er baseret på
		hvor mange timer man arbejder. En indkomst, som kommer uanset hvad du laver eller hvor du
		befinder dig. Det kræver dog at man sætter sig ind i at investerer, men så er det godt at du kan
		blive klogere lige her!
	</p>
